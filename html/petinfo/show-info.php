                <p>或许是受对爱犬的思念的启发，自4月起，川田利用&ldquo;UNIQUEST&rdquo;公司(总部位于大阪市)，开启宠物葬礼服务。猫、狗、仓鼠、兔子及鸟类均可对应，有价格为较一般行情稍高的不含税5万日元(约合人民币3288元)起的套餐。</p>
                <p>
                    <img style="width:100%" alt="日本宠物殡葬业大发展，宠物离世有葬礼服务还有悼念咖啡馆" src="<?= Yii::getAlias('@tasseturl'); ?>/petinfo/img/a3b14-9931.jpg" /></p>
                <p>近年来，不仅仅是火葬，饲主对于仪式也逐渐讲究起来。&ldquo;Pet Ceremony Wavy&rdquo;公司提供价格为不含税20万日元(约合人民币1.3万元)的宠物葬礼等，向着高价化推进。骨灰罐为大理石或九谷烧制成，以东京都内的富裕层为中心，颇有人气。</p>
                <p>
                    <img style="width:100%" alt="日本宠物殡葬业大发展，宠物离世有葬礼服务还有悼念咖啡馆" src="<?= Yii::getAlias('@tasseturl'); ?>/petinfo/img/081d4-3370.jpg" /></p>
                <p>报道称，东京原宿还有一家&ldquo;宠物丧失咖啡&rdquo;。该咖啡厅为&ldquo;In Loving Memory&rdquo;公司所开设的直营店，企划让来到店里的客人&ldquo;可以慢慢诉说&rdquo;与宠物之间的回忆。</p>
                <p>据悉，来到店里的客人8成是女性，也有从北海道及冲绳远道而来的客人。店长关口真季子表示：&ldquo;也有很多客人的寂寞无法治愈。希望能给予饲主的心灵哪怕一点点活力&rdquo;。</p>
