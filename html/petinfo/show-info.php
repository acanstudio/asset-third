                <p>据报道，家住大阪市的川田初美的爱犬一一将满14岁的雌性玩具贵宾犬&ldquo;Ran Chan&rdquo;，于2019年春天因心力衰竭去世。川田像举行普通葬礼一样吊唁了它：祭坛前装饰着鲜花，&ldquo;遗属&rdquo;凝视之处，放着藤条制成的小棺材，狗狗的&ldquo;遗体&rdquo;被置于其中，并将于仪式之后火葬。</p>
