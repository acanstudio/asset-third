                <p style="text-indent: 2em;">三、清显祖（清太祖之父，名塔克世）诸子&mdash;&mdash;清太祖-努尔哈赤（显祖第一子）；诚毅勇壮贝勒-穆尔哈齐（显祖第二子）；庄亲王-舒尔哈齐（显祖第三子）；二贝勒-阿敏（舒尔哈齐第二子）；郑亲王-济尔哈朗（舒尔哈齐第六子，铁帽子）；靖定贝勒-费扬武（舒尔哈齐第八子）；通达郡王-雅尔哈齐（显祖第四子）；笃义刚果贝勒-巴雅喇（显祖第五子）。</p>
                <p style="text-indent: 2em;">四、清太祖（名努尔哈赤，年号天命）诸子&mdash;&mdash;广略贝勒-褚英（太祖第一子）；安平贝勒-杜度（褚英第一子）；敬谨亲王-尼堪（褚英第三子）；礼亲王-代善（太祖第二子，铁帽子）；克勤郡王-岳讬（代善第一子，铁帽子）；颖亲王-萨哈璘（代善第三子）；顺承郡王-勒克德浑（萨哈璘第二子，铁帽子）；谦襄郡王-瓦克达（代善第四子）；镇国勤敏公-阿拜（太祖第三子）；镇国将军-汤古代（太祖第四子）；三贝勒-莽古尔泰（太祖第五子）；辅国公-塔拜（太祖第六子）；饶馀郡王-阿巴泰（太祖第七子）；温良贝子-博和讬（阿巴泰第二子）；端重亲王-博洛（阿巴泰第三子）；安亲王-岳乐（阿巴泰第四子）；清太宗-皇太极（太祖第八子）；镇国公-巴布泰（太祖第九子）；和硕贝勒-德格类（太祖第十子）；镇国将军-巴布海（太祖第十一子）；英亲王-阿济格（太祖第十二子）；辅国公-赖慕布（太祖第十三子）；睿亲王-多尔衮（太祖第十四子，铁帽子）；豫亲王-多铎（太祖第十五子，铁帽子）。</p>
                <p style="text-indent: 2em;">
                    <b>清太祖高皇帝的兄弟妻妾子女们</b>
                </p>
                <p style="text-indent: 2em;">
                    <strong>努尔哈赤共有4个弟弟</strong></p>
                <p style="text-indent: 2em;">有四个亲兄弟。</p>
                <p style="text-indent: 2em;">二弟：爱新觉罗&middot;穆尔哈齐，母为塔克世之妾李佳氏，异母弟，追封多罗诚毅勇壮贝勒。</p>
                <p style="text-indent: 2em;">三弟：爱新觉罗&middot;舒尔哈齐，母为宣皇后喜塔喇氏，同母弟，追封和硕庄亲王。</p>
                <p style="text-indent: 2em;">四弟：爱新觉罗&middot;雅尔哈齐，母为宣皇后喜塔喇氏，同母弟，追封多罗通达郡王。</p>
                <p style="text-indent: 2em;">五弟：爱新觉罗&middot;巴雅喇。母为塔克世次福晋哈达纳喇氏，异母弟，追封多罗笃义刚果贝勒。</p>
                <p style="text-indent: 2em;">
                    <strong>努尔哈赤共有16个儿子、8个女儿</strong></p>
                <p style="text-indent: 2em;">01&nbsp;长女（1578-1652），明万历六年（1578）二月二十二日生，母元妃佟佳氏哈哈纳札青。明万历十六年（1588年）嫁董鄂氏（亦称栋鄂、东果）何和礼，时年十一，因此称东果公主或东果格格。顺治九年（1652年）七月卒，年七十五，葬于今辽宁省灯塔市西大窑镇公安堡村东红宝石山南坡。康熙五十五年（1715年）追谥端庄，即为固伦端庄公主。</p>
                <p style="text-indent: 2em;">何和礼（1561-1624），明万历十六年（1588）投附努尔哈赤，当时已有妻子，努尔哈赤复以长女嫁之。何和礼是后金参决军国大事的开国五大臣之一。子六人，次子多积礼、第四子和顾图、第五子都类，在清朝初年皆有作为。其中第五子都类为公主所出。</p>
                <p style="text-indent: 2em;">02&nbsp;长子褚英（1580-1615），母为元妃佟佳&middot;哈哈纳扎青。褚英广有战功，努尔哈赤因其英勇，封号曰&ldquo;阿尔哈图土门&rdquo;，译言为&ldquo;广略&rdquo;，故褚英封号为广略贝勒。但是努尔哈赤的&ldquo;开国五大臣&rdquo;额亦都、费英东、何和礼、安费扬古和扈尔汉以及一些兄弟们与褚英不和，于是在努尔哈赤面前告状，努尔哈赤经过调查也发现了褚英的存在的问题，解除了他的兵权。褚英将不满溢于言表，后被软禁，之后褚英焚香诅咒告状的五大臣和兄弟，并扬言等掌权后处死五大臣和那些兄弟，被再一次告状。万历四十三年（1615年），褚英在被圈禁两年后，被努尔哈赤下决心处死，葬辽宁辽阳东京陵乡东京陵村。褚英有三子，长子杜度后为贝勒，三子尼堪后为亲王，都受到重用。</p>
                <p style="text-indent: 2em;">03&nbsp;次子代善（1583-1648），明万历十一年七月初三日（1583年8月19日）生，元妃佟佳&middot;哈哈纳扎青出。初称贝勒，三十五年四月，以击败乌拉功，赐号古英巴图鲁，明人或称为贵永介。天命元年四月，封和硕贝勒，参国、务国，人称为大贝勒。十一年八月，奉弟皇太极嗣父位。崇德元年四月，封和硕兄礼亲王。顺治二年春，至京师。五年戊子十月十一日（1648年11月25日）卒，年六十六。葬香山附近门头村礼亲王坟。代善孙子顺承郡王的家族墓地在房山西甘池、二龙岗几村左近。康熙十年六月，追谥曰烈。乾隆十九年九月，入祀盛京贤王祠。四十三年正月，配享太庙。特诏改见袭爵位之康亲王，复始封王号曰礼，世袭罔替。</p>
                <p style="text-indent: 2em;">长子多罗克勤郡王岳托，母嫡福晋李佳氏。克勤郡王为八大铁帽子王之一。</p>
                <p style="text-indent: 2em;">次子已革固山贝子硕托，母嫡福晋李佳氏。</p>
                <p style="text-indent: 2em;">三子和硕颖毅亲王萨哈磷，母继福晋叶赫纳喇氏。</p>
                <p style="text-indent: 2em;">四子多罗谦襄郡王瓦克达，母继福晋叶赫纳喇氏。</p>
                <p style="text-indent: 2em;">五子巴喇玛，母继福晋叶赫纳喇氏。</p>
                <p style="text-indent: 2em;">六子奉恩辅国公玛占，母侧福晋哈达纳喇氏。</p>
                <p style="text-indent: 2em;">七子和硕巽简亲王满达海，母三继福晋叶赫纳喇氏。</p>
                <p style="text-indent: 2em;">八子追封和硕惠顺亲王祜塞，母三继福晋叶赫纳喇氏。</p>
                <p style="text-indent: 2em;">04&nbsp;三子阿拜（1585-1648），镇国勤敏公。母为庶妃兆佳氏。天命十年，偕塔拜、巴布泰伐东海北路呼尔哈部，俘千五百户，还，太祖出城迎劳，授牛录章京。天聪八年，授梅勒额真。崇德三年，授吏部承政。四年，封三等镇国将军。六年，驻防锦州。八年，以老，罢承政。顺治四年，进二等。五年二月，卒。十年，追封谥。</p>
                <p style="text-indent: 2em;">05&nbsp;四子汤古代（1585-1640），镇国克洁将军。母为庶妃钮祜禄氏。事太宗，授固山额真。取永平四城，汤古代偕图尔格、纳穆泰守滦州。天聪四年，明兵攻滦州急，贝勒阿敏怯不敢援，遣巴都礼率数百人突围进，夜三鼓，入滦州。既，明兵以炮坏城，城楼火，汤古代等弃城奔永平。既还，太宗廷诘之，汤古代引罪请死。太宗曰：&ldquo;汝不能全师而归，杀汝何益？&rdquo;下所司论罪，免死，罢固山额真，夺所属人口，籍其家。八年，授三等梅勒章京。崇德四年，封三等镇国将军。五年，卒。他和他的后人葬在今沈阳市东陵区英达乡长岭子公家村，也叫七家子、公爷坟。</p>
                <p style="text-indent: 2em;">长子镇国将军聂克塞，母嫡妻富察氏。</p>
                <p style="text-indent: 2em;">次子镇国恪恭将军穆尔察，母继妻瓜尔佳氏。</p>
                <p style="text-indent: 2em;">06&nbsp;五子莽古尔泰（1587-1632），母为第二任大福晋富察氏。少年时期起，莽古尔泰便跟随努尔哈赤征伐乌喇部，英勇善战，连克六城。天命四年（1616年），受封为和硕贝勒。皇太极即位后，莽古尔泰数次进攻明军，屡立战功。死后却被族人们揭发有谋上之罪，结果被追夺其封爵。子额必伦。</p>
                <p style="text-indent: 2em;">07&nbsp;次女嫩哲（1587-1646），封和硕公主。生母为清太祖侧妃伊尔根觉罗氏，是阿巴泰的同母姐姐。据唐邦治先生考证，此女先嫁巴图鲁伊拉喀，后被伊拉喀遗弃，努尔哈赤杀死了伊拉喀。在天命年间又将此女嫁给早年投附的沾河部杨书之子，也是努尔哈赤的亲外甥郭尔罗氏达尔汉，因此称沾河公主或沾河格格。顺治三年（1646）七月卒，年六十。</p>
                <p style="text-indent: 2em;">达尔汉，明万历二十二年（1594）生，比嫩哲格格小7岁。天聪年间，达尔汉为满洲镶黄旗固山额真，为八大臣之一，积功封至一等子，官都统。崇德六年（1641）十月因争功被撤销固山额真职分并夺其世职。顺治元年（1644）卒，年五十一。</p>
                <p style="text-indent: 2em;">08&nbsp;六子塔拜（1589-1639），生于万历十七年（1589年）二月二十八日，母为庶妃钮祜禄氏。天命十年（1625年），伐东海北路呼尔哈部有功，授三等甲喇章京。天聪八年（1634年）升为一等甲喇章京，不久，封爵号三等辅国将军。崇德四年（1639年）卒。顺治十年（1653年）追封辅国公，谥号&ldquo;悫厚&rdquo;。塔拜葬在距离福陵3里远的东陵区满堂村北山。塔拜墓俗称塔王坟。</p>
                <p style="text-indent: 2em;">塔拜子八，有爵者三：额克亲、班布尔善、巴都海。</p>
                <p style="text-indent: 2em;">09&nbsp;七子阿巴泰（1589-1646），母为侧妃伊尔根觉罗氏。崇德元年，封饶馀贝勒。顺治元年四月，进郡王。二年，统左右两翼兵镇山东，剿满家洞土寇，寻还。三年，薨。</p>
                <p style="text-indent: 2em;">阿巴泰生子5人，有爵位者4人，即长子尚建封贝子，次子博和讬晋贝子，三子博洛晋亲王，四子岳乐晋亲王后降郡王。岳乐次女和硕柔嘉公主，葬门头沟区龙泉镇，埋葬地被当地人俗称为&ldquo;公主坟&rdquo;或&ldquo;耿王坟&rdquo;。和硕柔嘉公主育有一女耿氏，嫁纳兰明珠之子。</p>
                <p style="text-indent: 2em;">10&nbsp;三女莽古济（1590-1635），未正式册封，待遇视固伦公主，后革公主称号，削宗籍。生母为第二任大福晋富察氏衮代。第五子莽古尔泰、第十子德格类、第十六子费扬古是她同母兄弟。明万历二十九年（1601）嫁给哈达部纳喇氏吴尔古代，时年十二。因此称哈达公主或哈达格格。天命末年，吴尔古代卒。天聪元年（1627）复嫁给蒙古敖汉部博尔济吉特氏琐诺木杜凌，赐以开原之地。九年（1635）莽古济因骄暴被削除格格称号而为民，并且禁止其与亲属往来。是年又被属下家权冷僧机告发，揭发她曾与同母兄弟莽古尔泰、德格类一起盟誓谋逆夺权，其丈夫琐诺木杜凌并出证，结果被处死。与吴尔古代生两女，一女嫁豪格为嫡福晋，后被豪格杀死；另一女嫁岳托为继福晋，后为岳托殉葬。</p>
                <p style="text-indent: 2em;">11&nbsp;八子皇太极（1592-1643），清太宗。万历二十年十月二十五日生，母为孝慈高皇后叶赫那拉氏。1626年，继位后金可汗，改年号为天聪，史称&ldquo;天聪汗&rdquo;。1636年，皇太极于盛京即皇帝位，改国号为&ldquo;大清&rdquo;，改元崇德。</p>
                <p style="text-indent: 2em;">12&nbsp;九子巴布泰（1592-1655），镇国恪僖公。母为庶妃嘉穆瑚觉罗氏。天命十年，偕阿拜、塔拜伐东海北路呼尔哈部，有功。十一年，命理正黄旗事。天聪四年，从阿敏驻永平。明兵攻滦州，巴布泰不能御，坐罢。八年，授梅勒额真。从伐明，克保安州。巴布泰匿所获不以闻，复坐罢。崇德六年，授三等奉国将军。顺治元年，从入关，逐李自成至庆都。二年，进一等。三年，从勒克德浑伐湖广，战安远、南漳、西峰口、关王岭、襄阳，屡破敌。四年，进辅国公。六年，偕务达海讨姜瓖，进镇国公。十二年正月，卒，予谥。</p>
                <p style="text-indent: 2em;">长子奉恩辅国公噶布喇，万历三十八年七月十一日辰时生，母嫡妻李佳氏；康熙十六年十一月二十九日午时卒，年六十九岁。</p>
                <p style="text-indent: 2em;">次子明聆，崇德八年五月初四日亥时生，母三继妻费莫氏；顺治五年九月十一日丑时卒，年五岁。</p>
                <p style="text-indent: 2em;">三子已革镇国将军祜锡禄，顺治十一年五月初五日寅时生，母四继妻李佳氏；康熙三十七年八月十九日申时卒，年四十四岁。</p>
                <p style="text-indent: 2em;">13&nbsp;十子德格类（1592-1635），生于明朝万历二十年（1592年）十一月十三，母为第二任大福晋富察氏。封贝勒。天聪九年（1635年）十月初二，德格类病死。三子：德克西克，费雅塔，瓦克扎。</p>
                <p style="text-indent: 2em;">14&nbsp;四女穆库什（1595-1659），革和硕公主。生母为庶妃嘉穆瑚觉罗氏，与第九子巴布泰、第十一子巴布海、第五女、第六女同母。明万历三十六年（1608）嫁海西女真乌拉部贝勒纳喇氏布占泰，时年十四。随着布占泰与努尔哈赤矛盾的加深，万历四十年（1612）布占泰欲射以鸣镝，努尔哈赤率兵将其接回。后复嫁后金开国元勋巴图鲁钮祜禄氏额亦都，生额亦都第十六子遏必隆；第十七子索索浑费扬古，殇。一女，钮祜禄氏，额亦都之女（亦说为布占泰之女），嫁褚英的第三子尼堪。额亦都去世后，再嫁额亦都第八子钮祜禄氏图尔格。图尔格，生于明万历二十四年（1596）。崇德初，穆库什封和硕公主。崇德二年（1637），其女因嫁尼堪未生育，将女仆之女冒为己生。事情暴露后，穆库什被革除和硕公主称号，图尔格也被免职。是年穆库什与图尔格离异，由其同母兄弟巴布泰、巴布海养赡。图尔格后因军功复职，积功晋三等公，顺治二年（1645）卒。顺治十六年（1659）五月，穆库什卒，年六十五。</p>
                <p style="text-indent: 2em;">15&nbsp;十一子巴布海（1596-1643），母为庶妃嘉穆瑚觉罗氏。初授牛录章京。天聪八年，授一等甲喇章京。尝命偕镇国将军阿拜祭陵，巴布海不待阿拜，先往祭。牛未至，取民牛代，以祭牛偿民，民以小不受，讼焉，罚银三十偿民，又不与，再讼。巴布海闻上，上责其愚黯，且谓其受制於妻，妻，扬古利女也。崇德四年，授梅勒额真，封镇国将军。七年，巴布海语固山额真谭泰曰：&ldquo;原罢我梅勒额真。堪为梅勒额真者，多於草木！&rdquo;谭泰语折之，誓曰：&ldquo;若口与心违者，天日鉴之！&rdquo;图海奉命差择牛录贫富，巴布海曰：&ldquo;我所领牛录甚富。&rdquo;语闻，巴布海曰：&ldquo;我非太祖之子欤？谭泰等顾厚诬我。&rdquo;廷鞫皆实，罪当死，上宽之，但夺爵。世祖即位，有为飞书讦谭泰者，投一等公塔瞻第。鞫其仆，谓得之巴布海家。内监逮讯，不承，巴布海及其妻并子阿喀喇皆坐死，籍其家予谭泰。顺治九年，谭泰诛，乃以其孥及遗产畀巴布泰。</p>
                <p style="text-indent: 2em;">子阿喀喇，天命十年八月初五日巳时生，母嫡妻瓜尔佳氏，吴尔堪之女；崇德八年八月因父罪处死，年十八岁。嫡妻纳喇氏，莽佳之女，无子女。</p>
                <p style="text-indent: 2em;">16&nbsp;五女（1597-1613），无封。生母为庶妃嘉穆瑚觉罗氏，与第九子巴布泰、第十一子巴布海、第四女穆库什、第六女同母。明万历三十六年（1608）嫁后金开国元勋巴图鲁额亦都之次子党奇为妻，时年十二。党奇自幼即受到努尔哈赤的钟爱，估宠而骄，成为额驸后，更是骄横，经常在努尔哈赤诸子面前行止无礼。额亦都虽多次训斥，但始终不改。为正门庭，是年额亦都将党奇杀死。明万历四十一年（1613），皇五女卒，年十七。</p>
                <p style="text-indent: 2em;">17&nbsp;六女（1600-1646），无封。生母为清太祖庶妃嘉穆瑚觉罗氏，与第九子巴布泰、第十一子巴布海、第四女穆库什、第五女同母。明万历四十一年（1613）嫁海西女真叶赫部纳喇氏苏鼐，时年十四。苏鼐曾官至都督，后金天聪九年（1635）因故革去世职。崇德五年（1640）卒。第六女于顺治三年（1646）卒，年四十七。</p>
                <p style="text-indent: 2em;">18&nbsp;七女（1604-1685），封乡君品级。生母为努尔哈赤庶妃伊尔根觉罗氏。明万历三十二年（1604）三月初十日生。天命四年（1619）嫁给骑都尉纳喇氏鄂托伊，时年十六。崇德六年（1641）鄂托伊阵亡。第七女乡君于康熙二十四年（1685）卒，年八十二。</p>
                <p style="text-indent: 2em;">19&nbsp;十二子阿济格（1605-1651），罢英亲王。母为大妃乌拉那拉氏。初授台吉，天命十一年（1626年）以军功授贝勒，崇德元年（1636年）晋封武英郡王，清顺治元年（公元1644年）封和硕英亲王。地位仅次于四大贝勒。顺治五年（1648年）又以平西大将军督师征讨姜瓖在大同的叛乱。在九江，又招降明军左梦庚部。他要求封叔王，被摄政王多尔衮拒绝。多尔衮死后，他企图摄政，被削爵幽禁赐死。葬京师城东八王坟。五子：和度，傅勒赫；五女为纳兰性德生母。</p>
                <p style="text-indent: 2em;">20&nbsp;十三子赖慕布（1611-1646），辅国介直公。母为庶妃西林觉罗氏。天聪八年，授牛录章京。崇德四年，与议政。七年，从阿济格伐明，败宁远兵。上御笃恭殿赉师，阿济格不待赏先归。赖慕布坐不劝阻，夺职，罢议政。顺治二年，封奉恩将军。三年，卒。十年五月，追封谥。</p>
                <p style="text-indent: 2em;">21&nbsp;十四子多尔衮（1612-1650.12.31），睿忠亲王（追封成宗义皇帝，后罢）。明万历四十年十月廿五日（1612年11月17日）生于赫图阿拉（今辽宁新宾老城），母为大妃乌拉那拉&middot;阿巴亥。1626年封贝勒；1636年因战功封和硕睿亲王；1643年辅政，称摄政王；1644年指挥清军入关，清朝问鼎中原，先后封叔父摄政王、皇叔父摄政王、皇父摄政王；顺治七年十二月初九日（1650年12月31日）因狩猎坠马不治而亡，先追尊为成宗义皇帝，后被顺治帝追论谋逆罪削爵；1778年乾隆帝为其平反，复睿亲王封号，评价其&ldquo;定国开基，成一统之业，厥功最著&rdquo;。葬东直门外新中街附近睿忠亲王墓。</p>
                <p style="text-indent: 2em;">多尔衮的妻妾们：</p>
                <p style="text-indent: 2em;">天命八年（1624）五月二十八日，与科尔沁博尔济吉特氏（1610-1649）成婚。博尔济吉特氏是多尔衮的原配大福晋，是孝端文皇后的从侄女，与孝庄文皇后同一个曾祖父。</p>
                <p style="text-indent: 2em;">天聪年间，娶佟佳氏。这是第一位侧福晋。佟佳氏，建州女真本部尚书孟噶图之女。</p>
                <p style="text-indent: 2em;">天聪五年娶妾扎鲁特博尔济吉特氏，蒙古扎鲁特部贝勒之女。</p>
                <p style="text-indent: 2em;">天聪六年二月，娶第二位侧福晋扎鲁特博尔济吉特氏扎尔莽，蒙古扎鲁特部根杜尔台吉之女。</p>
                <p style="text-indent: 2em;">天聪七年，娶第三位侧福晋科尔沁博尔济吉特氏。</p>
                <p style="text-indent: 2em;">天聪八年娶妾科尔沁博尔济吉特氏，蒙古科尔沁部贝勒满珠习礼（斋桑第四子）之女。</p>
                <p style="text-indent: 2em;">天聪九年十月二十六日，多尔衮前往科尔沁迎娶第四位侧福晋博尔济吉特氏巴特玛。巴特玛是蒙古科尔沁和硕福妃之女，多铎继福晋的姐妹。蒙古科尔沁和硕福妃，清人称之为科尔沁大妃，是孝端文皇后之母，孝庄文皇后和敏惠恭和元妃之祖母。其第一任丈夫是科尔沁部札日固齐（断事官）、贝勒莽古斯，是孝端文皇后之生父，孝庄文皇后和敏惠恭和元妃之祖父。莽古斯去世后，科尔沁大妃复嫁给莽古斯之第三子台吉索诺木。崇德二年，皇太极追封莽古斯为和硕福亲王，册封科尔沁大妃为和硕福妃。</p>
                <p style="text-indent: 2em;">崇德二年（1637）娶妾李氏，称小福晋。李世绪之女，朝鲜宗室女子。是多尔衮征服朝鲜后带回盛京的。引人瞩目的是，这位朝鲜姑娘在第二年给妻妾成群、却始终没有一儿半女的多尔衮生了一个女儿东莪，是多尔衮唯一的血脉，这一年多尔衮26岁。东莪，多尔衮独生女儿，也是他唯一的骨血。生于崇德三年（1638）。东莪这个名字，是多尔衮为纪念其长姊东果公主（东果为东果格格夫家的部落名和姓氏&ldquo;董鄂&rdquo;，不是其名）所取的名字。在多尔衮死后，东莪被顺治下旨交于信郡王多尼府中看管，这是清史中关于这个女孩子唯一的记载，此后，史册中再无任何记载，不知所终。</p>
                <p style="text-indent: 2em;">崇德五年三月，娶康惠淑妃抚养的蒙古女。</p>
                <p style="text-indent: 2em;">顺治三年娶妾科尔沁博尔济吉特氏，蒙古科尔沁部土谢图汗巴达礼之女。</p>
                <p style="text-indent: 2em;">顺治七年正月娶豪格的一位福晋，这位福晋是他原配大福晋的亲妹妹。科尔沁博尔济吉特氏，蒙古科尔沁部吉桑阿尔寨之女。</p>
                <p style="text-indent: 2em;">顺治七年五月娶十六岁的朝鲜义顺公主，是多尔衮在原配去世后娶的继室大福晋。义顺公主是金林郡公李开音之女。不过，她嫁过来几个月后多尔衮就死了，据说这位义顺公主后来归了安亲王岳乐，或云其寡居后被接回了朝鲜，待考。</p>
                <p style="text-indent: 2em;">妾察哈尔公齐特氏，蒙古察哈尔部布延图台吉之女。</p>
                <p style="text-indent: 2em;">妾博尔济吉特氏，蒙古杜思噶尔卓农台吉之女。</p>
                <p style="text-indent: 2em;">妾济尔莫特氏，帮图武之女。</p>
                <p style="text-indent: 2em;">22&nbsp;八女聪古伦（1612-1646），封和硕公主。明万历四十年（1612）十二月初七日生，生母为侧妃叶赫纳喇氏。天命十年（1625）嫁蒙古喀尔喀部台吉博尔济吉特氏固尔布锡，时年十四。固尔布锡于天命八年（1621）投附努尔哈赤，授二等子，并赐号青卓礼克固，十年（1625）又将幼女嫁给他。天聪年间为兵部承政。顺治七年（1650）晋一等子。八年（1651）坐罪削爵，晚年复爵，十八年（1661）卒，年六十五。聪古伦于顺治三年（1646）二月卒，年三十五。</p>
                <p style="text-indent: 2em;">23&nbsp;十五子多铎（1614-1649），豫通亲王。明万历四十二年二月二十四日（1614年4月2日）出生，母为大妃乌拉那拉氏阿巴亥，与阿济格、多尔衮为同母兄弟。天命五年（1620年）封为和硕额真，十三岁时，封贝勒，统正白旗，参与礼部和兵部政事。崇德元年（1636），封和硕豫亲王。后因狂放不羁，风流好色，崇德三年（1638年）因军前私自携带妓女，降为多罗贝勒。崇德六年（1641年），参与松锦大战，获大捷，生俘洪承畴，晋多罗郡王。清世祖顺治元年（1644年）随清军入山海关，在山海关之战中大败李自成大顺军，入北京，晋亲王。十月下河南、入陕西，顺治二年一月攻入潼关、西安；二月攻南明，钱谦益出降；四月陷扬州，杀史可法，制造&ldquo;扬州十日&rdquo;屠城。五月入南京，俘南明弘光帝朱由嵩，六月占浙江，后班师回京，加封和硕德豫亲王。顺治三年（1646年），以扬威大将军征讨蒙古苏尼特部腾机思。乾隆帝称其为&ldquo;开国诸王战功之最&rdquo;。顺治四年（1647）七月加封辅政叔德豫亲王。顺治六年三月染天花疾亡，享年仅三十六岁。多铎的墓地建在建国门外大北窑，老地名为苗家地。</p>
                <p style="text-indent: 2em;">多铎子八，有爵者四：多尼、董额、察尼、多尔博、费扬古。</p>
                <p style="text-indent: 2em;">24&nbsp;十六子费扬果，黜宗室。母不详。一说，母为继妃富察氏。太宗时，坐罪赐死，削宗籍。康熙五十二年，圣祖命莽古尔泰尼雅、德格类子孙复宗籍。费扬果曾孙三等侍尼雅罕呈宗人府请复宗籍，宗人府以闻，圣祖曰：&ldquo;此事朕知之，但不详耳。费扬果，太祖子，太宗时因获大罪诛死者。&rdquo;命复宗籍，赐红带。据考证，费扬果生于天命五年（1620年），死的年岁就不可考了，据说是在约崇德五年（1640年）死的。而另一种说法是约在清太宗天命九年（1635年）死的。四子。</p>
                <p style="text-indent: 2em;">&nbsp;
                    <b style="text-indent: 2em;">努尔哈齐生育过子女的妻妾</b></p>
                <p style="text-indent: 2em;">01&nbsp;元妃佟佳氏，努尔哈赤元配嫡福晋，努尔哈赤入赘，名哈哈纳扎青，塔本巴晏之女。子二：长子褚英，次子代善；女一：长女，嫁董鄂氏（东果氏）何和礼，称东果格格，封固伦公主。</p>
                <p style="text-indent: 2em;">02&nbsp;庶妃兆佳氏，喇克达之女。子一：三子阿拜。</p>
                <p style="text-indent: 2em;">03&nbsp;庶妃钮祜禄氏，博克瞻之女。子二：四子汤古代；六子塔拜。</p>
                <p style="text-indent: 2em;">04&nbsp;继妃富察氏，努尔哈赤第二任大福晋，名衮代，莽塞杜诸之女。子二：五子莽古尔泰，十子德格类；女一：三女莽古济。富察氏初嫁努尔哈赤三祖索长阿孙威准，生子昂阿拉按昂阿拉；万历十三年复嫁努尔哈赤。天命五年二月，以窃藏金帛，迫令自尽（一说，被其子莽古尔泰所杀）；葬福陵之外。</p>
                <p style="text-indent: 2em;">05&nbsp;侧妃伊尔根觉罗氏，札亲巴晏之女。子一：七子阿巴泰；女一：次女嫩哲。</p>
                <p style="text-indent: 2em;">06&nbsp;孝慈高皇后叶赫那拉氏，名孟古哲哲。叶赫贝勒杨吉努之女，叶赫贝勒纳林布禄（那林孛罗）、金台什（金台失）之妹。明万历三年乙亥生。十六年九月嫁努尔哈赤为侧福晋。三十一年九月二十七日薨，年二十九。崇德元年四月，皇太极追尊其母为孝慈武皇后。顺治元年九月，升祔太庙。康熙元年四月，将太祖谥由&ldquo;武&rdquo;改为&ldquo;高&rdquo;，即为孝慈高皇后。子一，八子皇太极，即清太宗。孝慈高皇后生前是否当过大福晋，尚有争议。一说，她一直是侧福晋；二说，继妃被废后，她成为第三任大福晋。</p>
                <p style="text-indent: 2em;">07&nbsp;庶妃嘉穆瑚觉罗氏，名真哥，贝勒浑巴晏之女。子二：九子巴布泰，十一子巴布海；女三：四女穆库什；五女，嫁达启；六女，嫁苏纳。</p>
                <p style="text-indent: 2em;">08&nbsp;庶妃伊尔根觉罗氏，察弼之女。女一：七女，封乡君品级，嫁鄂托伊。</p>
                <p style="text-indent: 2em;">09&nbsp;大妃乌拉那拉氏，名阿巴亥。乌拉贝勒满泰之女。明万历十八年生。二十九年十一月，其叔父贝勒布占泰送她嫁努尔哈赤为侧福晋。前一任大福晋去世后，立为大福晋。天命十一年八月十一日，太祖崩；次日，大妃以身殉，年三十有七。顺治七年八月，多尔衮追封其母为孝烈武皇后，升祔太庙。八年二月，因多尔衮得罪，罢皇后名份，夺谥号、黜庙享。子三，十二子阿济格；十四子多尔衮；十五子多铎。</p>
                <p style="text-indent: 2em;">10&nbsp;庶妃西林觉罗氏，奋杜里哈斯祜之女。子一：十三子赖慕布。</p>
                <p style="text-indent: 2em;">11&nbsp;侧妃叶赫那拉氏，杨吉砮之女，孝慈高皇后之妹。万历三十八年嫁努尔哈赤为侧福晋。女一：八女聪古伦，封和硕公主。</p>
                <p style="text-indent: 2em;">
