                <p style="TEXT-INDENT:2em">
                    <strong>
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a></strong>（公元1636年～1912年，一说按照1616年努尔哈赤称汗起算，一说按照1636年皇太极改国号起算，1644年起为全国性政权），又称大清（鞑清），简称清，是
                    <a href="http://m.114.xixik.com/chinese-dynasties/" style="color:#000" title="中国历史">中国历史</a>上最后一个封建王朝，也是
                    <a href="http://m.114.xixik.com/chinese-dynasties/" style="color:#000" title="中国历史">中国历史</a>上第二个由少数民族（建州女真，后改为&ldquo;满族&rdquo;）建立并统治全国的封建王朝。共传十二帝 ，享国祚二百六十八年。康熙雍正乾隆三代所谓的&ldquo;康乾盛世&rdquo;是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>的鼎盛时期，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>中后期政治僵化、文化专制、闭关锁国、内部矛盾激化、逐步落后于世界。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>处于封建社会晚期，盛衰隆替，风云变换。它的崛起为封建社会注入了新的生机，它的衰落又导致了封建社会的瓦解。作为中国漫长的封建社会的最后一页，在二百多年的漫长岁月中，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>奠定中国而今版图，也为中华民族留下了大量的失败与屈辱的辛酸记录。</p>
                <p style="TEXT-INDENT:2em">明万历四十四年（1616年），建州女真部首领努尔哈赤在赫图阿拉（今辽宁新宾）称&ldquo;覆育列国英明汗&rdquo;，国号&ldquo;大金&rdquo;（史称后金）。1620年10月20日，在赫图阿拉建立大金国，即大汗位，改
                    <a href="http://m.114.xixik.com/nianhao/" style="color:#000" title="年号">年号</a>为天聪。成为后金大汗。天聪十年四月十一日（1636年5月15日），皇太极在盛京改国号为大清。1644年李自成率大顺军攻占
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="color:#000" title="明朝">明朝</a>国都北京。吴三桂降清，清军趁势入关，政治上推行剃发易服，军事上打击农民军和南明诸政权，逐步掌握全国。</p>
                <p style="TEXT-INDENT:2em">从顺治元年（1644年）
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>入关到1912年中国民国成立，清帝退位，清代统治全国共268年。
                    <strong>
                        <a href="http://www.xixik.com/content/9ce1ab7d9b6c435a" style="color:#000;" title="清朝12位皇帝">清朝12个皇帝</a></strong>包括入关前的两帝，努尔哈赤，皇太极；入关后10帝：顺治，康熙，雍正，乾隆，嘉庆，道光，咸丰，同治，光绪，宣统；
                    <a href="http://www.xixik.com/content/6ccbdea0ca2d796e" title="没称帝却像皇帝的中国最后女强人慈禧太后轶事">慈禧虽非皇帝，却独断朝纲，但始终不敢称帝</a>。其中公认的努尔哈赤并未称帝，他统一了女真各部，于1616年建立了大金国（史称&ldquo;后金&rdquo;），不是清；而且是他的称号不是皇帝，而是&ldquo;覆育列国英明汗&rdquo;，所以他只是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>的奠基人而已。真正开始称帝的是皇太极，他在盛京（即沈阳）称帝，并该国号为&ldquo;清&rdquo;，所以，无论从那个角度来讲，皇太极才是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>第一个皇帝，但是一般在计算清朝皇帝时算上努尔哈赤。</p>
                <p style="TEXT-INDENT:2em">1840年爆发了中英鸦片战争，多遭列强入侵，主权和领土严重丧失。第二次鸦片战争之后，开始了近代化的探索，地主阶级进行了洋务运动。甲午战争之后民族危机进一步加深，以康梁为首的维新派开始进行戊戌变法，但是变法失败。1900年的夏天，八国联军为了镇压义和团运动维护在华利益侵略中国。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>沦为半殖民地半封建社会。1911年，辛亥革命爆发，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color:#000" title="清朝">清朝</a>统治瓦解，1912年2月12日，清帝被迫退位。从此结束了中国两千多年来的封建帝制转入了民主革命时期。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>君主是指
                    <strong>
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>
                        <a href="http://m.114.xixik.com/chinese-emperors/" style="color: rgb(0, 0, 0);" title="皇帝">皇帝</a></strong>，以及
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>前身后金政权的大汗，总共12位。从皇太极建立
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>开始共有11位
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="皇帝">皇帝</a>。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>入关以后共有十位
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="皇帝">皇帝</a>。&nbsp;</p>
