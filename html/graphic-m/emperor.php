                <p style="text-indent: 2em;">
                    <strong>努尔哈赤皇后</strong></p>
                <p style="text-indent: 2em;">1.孝慈高皇后（1575年—1603年10月31日）：</p>
                <p style="text-indent: 2em;">叶赫那拉·孟古，生于1575年（明朝万历三年），叶赫部首领杨吉砮之女。在1588年10月（万历十六年九月），十四岁时嫁努尔哈赤。叶赫那拉氏庄敬聪惠，端庄贤德。婚后四年生下一子，即清太宗皇太极。1603年10月31日（万历三十一年九月二十七日）病逝，享年二十九岁，1624年（后金天命九年）迁葬东京陵，1629年3月7日（天聪三年二月十三日）迁葬福陵。1636年（天聪十年）皇太极称帝，尊其母为孝慈皇后，经累朝加谥，最后的谥号是“孝慈昭宪敬顺仁徽懿德庆显承天辅圣高皇后”。</p>
                <p style="text-indent: 2em;">2.孝烈武皇后（1589年—1626年9月1日）：阿巴亥</p>
                <p style="text-indent: 2em;">乌拉那拉·阿巴亥继室大福晋，海西女真乌拉部（今天吉林省吉林市北30公里的乌拉街满族乡）贝勒满泰之女。1601年（万历二十九年）十二岁的阿巴亥在叔父布占泰的亲自护送下到赫图阿拉，嫁给了比她年长31岁的努尔哈赤为侧福晋。两年之后被册为大福晋。1606年（万历三十四年）生十二子英亲王阿济格、1612年生十四子睿忠亲王多尔衮，1614年生十五子豫通亲王多铎。1620年（天命五年）小福晋德因泽告发阿巴亥，努尔哈赤以私藏金银的罪名而将其“离弃”。次年又复为大福晋。</p>
                <p style="text-indent: 2em;">1626年9月1日（天命十一年/明天启六年八月十一日）努尔哈赤病逝于叆鸡堡。八月十二日卯时，四大贝勒宣称先帝曾有遗言，大福晋从殉。三十七岁的阿巴亥被逼殉葬，死时距努尔哈赤去世不到一天，与努尔哈赤同棺敛葬于福陵。顺治初年，阿巴亥的次子多尔衮摄政。顺治七年，阿巴亥得到“孝烈恭敏献哲仁和赞天俪圣武皇后”的谥号，牌位入太庙。同年年底，多尔衮暴病身亡。阿巴亥的“孝烈皇后”又被罢谥，牌位也被赶出太庙。</p>
                <p style="text-indent: 2em;">皇太极皇后 1.孝端文皇后（1599年5月13日—1649年5月27日）：</p>
                <p style="text-indent: 2em;">博尔济吉特·哲哲，清太宗爱新觉罗·皇太极正宫皇后。生于1599年5月13日（万历二十七年四月十九日），蒙古科尔沁贝勒莽古思之女。清太宗时为国君福晋，正宫皇后，居中宫。清世祖时尊为皇太后。顺治六年5月27日（1649年四月十七日）崩，年五十一。顺治七年二月葬昭陵。雍正、乾隆累加谥，谥曰孝端正敬仁懿哲顺慈僖庄敏辅天协圣文皇后。无子，生三女，下嫁额哲、奇塔特、巴雅思祜朗。福晋、中宫大福晋（清宁宫）、皇后、皇太后</p>
                <p style="text-indent: 2em;">2.孝庄文皇后（1613年3月28日—1688年1月27日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/24f19-2191.jpg" /></p>
                <p style="text-indent: 2em;">博尔济吉特·布木布泰。1613年3月28日（明万历四十一年二月初八日）出生，古科尔沁部（在今通辽）贝勒寨桑之次女。清太宗爱新觉罗·皇太极之妃，孝端文皇后的侄女，顺治帝爱新觉罗·福临的生母。是史上有名的贤后，一生培育、辅佐顺治、康熙两代君主。</p>
                <p style="text-indent: 2em;">顺治皇后 1.孝惠章皇后（1641年11月5日—1718年1月7日）：</p>
                <p style="text-indent: 2em;">博尔济吉特·荣惠，生于1641年11月5日（后金崇德六年十月三日），科尔沁贝勒绰尔济之女，世祖废后之侄女。清世祖时为妃、皇后，圣祖时尊为仁宪皇太后。1718年1月7日（康熙五十六年十二月初六）卒，年七十七。1718年（康熙五十七年）上谥，葬孝陵（清东陵）之东，称孝东陵，祔太庙。雍正、乾隆朝累加谥，为孝惠仁宪端懿慈淑恭安纯德顺天翼圣章皇后。无子女。</p>
                <p style="text-indent: 2em;">2.孝康章皇后（1630年—1663年3月20日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/faf1a-2896.jpg" /></p>
                <p style="text-indent: 2em;">佟佳·念锦，1630年（天聪三年）生，少保、固山额真佟图赖女，后初入宫，为世祖妃。1654年（顺治十一年）春，妃诣太后宫问安，将出，衣裾有光若龙绕，太后问之，知有妊，谓近侍曰：“朕妊皇帝实有斯祥，今妃亦有是，生子必膺大福。”5月4日（三月十八日），圣祖生。圣祖康熙即位，尊为皇太后。1663年3月20日（康熙二年二月初九），崩，年二十四。初上徽号曰慈和皇太后。及崩，葬孝陵，上谥。雍正、乾隆累加谥，曰孝康慈和庄懿恭惠温穆端靖崇文育圣章皇后。后家佟氏，本汉军，上命改佟佳氏，入满洲。</p>
                <p style="text-indent: 2em;">3.孝献皇后（1639年—1660年9月23日）：</p>
                <p style="text-indent: 2em;">董鄂·乌云珠，世称董鄂妃，独为帝所盛宠，清朝第一宠妃，内大臣鄂硕之女，费扬古之姊。出生时间：1639年（崇德四年）生，1656年9月（顺治十三年八月）进宫，：1656年10月12日（顺治十三年八月二十五日）封贤妃，1656年11月14日（顺治十三年九月二十八日），封皇贵妃，并大赦天下。1657年11月12日（顺治十四年十月初七）生下一个儿子不料未及百日便夭折，封和硕荣亲王。1660年9月23日（顺治十七年八月十九日）仙逝于承乾宫，顺治深为哀恸、寻死寻活。10月3日（八月二十九日）追封为皇后。合葬孝陵，追尊谥为：孝献庄和至德宣仁温惠端敬皇后。</p>
                <p style="text-indent: 2em;">康熙皇后 1.孝诚仁皇后（1654年2月3日—1674年6月6日）：</p>
                <p style="text-indent: 2em;">赫舍里·芳仪皇后，生于1654年2月3日（顺治十年十二月十七日），满洲正黄旗人，领侍卫内大臣咯布拉之女，世袭一等公索额图的亲侄女，辅政大臣索尼的孙女。康熙四年册封为皇后，时年13岁。康熙八年生皇二子（未序齿）承祜（2岁夭折）；1674年6月6日（康熙十三年五月初三）生皇七子（序齿皇次子）胤礽后，因难产去世享年22岁，她是康熙皇帝的第一位皇后（元配），葬清东陵之景陵。初谥’仁孝皇后“，雍正元年改谥”孝诚“。全部谥号为：孝诚恭肃正惠安和淑懿恪敏俪天襄圣仁皇后。</p>
                <p style="text-indent: 2em;">2.孝昭仁皇后（1654年-1678年3月18日）：</p>
                <p style="text-indent: 2em;">钮祜禄·东珠，辅政大臣一等公遏必隆女，鳌拜义女，满洲镶黄旗人，温僖贵妃的姐姐，生于顺治十年。初为妃，1677年9月18日（康熙十六年八月二十二日）册封为第二任皇后，1678年3月18日（康熙十七年二月二十六日）死。葬清东陵之景陵。谥号：孝昭静淑明惠正和安裕端穆钦天顺圣仁皇后。</p>
                <p style="text-indent: 2em;">3.孝懿仁皇后（？-1689年8月24日）：</p>
                <p style="text-indent: 2em;">佟佳·仙蕊，满洲镶黄旗人，领侍卫内大臣佟国维之女，康熙帝生母孝康章皇后的嫡亲侄女，康熙的表姐妹，生年不详。贵妃、皇后，康熙第三任皇后，也是他的嫡亲表妹，康熙十六年八月册封为贵妃，二十年晋升为皇贵妃。二十二年生皇八女（早殇）。1689年8月23日（康熙二十八年七月初九日）封后，翌日崩逝，实际上只当了一天不到的皇后，成为我国历史上在位时间最短的皇后。葬清东陵之景陵。初谥“孝懿皇后”，后雍正乾隆嘉庆累加谥，全部谥号：孝懿温诚端仁宪穆和恪慈惠奉天佐圣仁皇后。收胤禛为养子。</p>
                <p style="text-indent: 2em;">4.孝恭仁皇后（1660年—1723年6月25日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/8054d-4901.jpg" /></p>
                <p style="text-indent: 2em;">乌雅·德宛，满洲正黄旗人，护军参领威武之女，生于顺治十七年。1677年（康熙十七年十月三十日）生皇四子胤禛，即雍正帝。1678年（十八年）封为德嫔。次年生皇六子胤祚；1680年（二十年十二月二十日）为德妃；二十一年生皇七女，殇；二十二年生皇九女温宪公主；二十五年生皇十二女，殇；二十七年生皇十四子胤禵。康熙帝驾崩后，德妃被尊为仁寿皇太后，1723年6月25日（雍正元年五月二十三日）丑刻薨，享年64岁，葬清东陵之景陵。谥号：孝恭宣惠温肃定裕慈纯钦穆赞天承圣仁皇后。</p>
                <p style="text-indent: 2em;">
                    <strong>雍正皇后</strong></p>
                <p style="text-indent: 2em;">1.孝敬宪皇后（？—1731年10月29日）：</p>
                <p style="text-indent: 2em;">乌拉那拉·舒兰，步兵统领费扬古女，生母觉罗氏追封多罗格格，是贝子穆尔祜第四女，穆尔祜长女嫁乃孝献皇后嫡母。育有一子，弘晖，八岁夭折。雍正为皇子时，圣祖册后为嫡福晋。1723年（雍正元年），册为皇后。在掌管六宫时，和嫔妃、宫娥之间关系也很好。1731年10月29日（雍正九年九月二十九日）逝世。与雍正合葬于清西陵之泰陵。谥号：孝敬恭和懿顺昭惠庄肃安康佐天翊圣宪皇后。</p>
                <p style="text-indent: 2em;">2.孝圣宪皇后（1693年1月1日—1777年3月2日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/40267-8943.jpg" /></p>
                <p style="text-indent: 2em;">钮祜禄氏，生于1693年1月1日（康熙三十一年十一月二十五日），满洲镶黄族人四品典仪官凌柱之女。1704年（康熙四十三年），13岁入侍雍王府，名号为格格，为雍正胤禛之侧福晋。1711年（康熙五十年）八月十三日，生下后来的乾隆皇帝弘历。1723年3月（雍正元年二月），册封为熹妃，入居景仁宫；1730年（雍正八年），进熹贵妃；1735年（雍正十三年）九月，弘历即位，尊封为崇庆皇太后，移居慈宁宫。卒于1777年3月2日（乾隆四十二年正月二十三），葬泰东陵，享年85岁。谥：孝圣慈宣康惠敦和诚徽仁穆敬天光圣宪皇后。</p>
                <p style="text-indent: 2em;">
                    <strong>乾隆皇后</strong></p>
                <p style="text-indent: 2em;">1.孝贤纯皇后（1712年3月28日—1748年4月8日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/12b52-5905.jpg" /></p>
                <p style="text-indent: 2em;">富察·英琦，孝贤皇后生于1712年3月28日（康熙五十一年二月二十二日）。1727年9月3日（雍正五年七月十八日）奉旨与皇四子弘历成婚，为嫡福晋。1728年雍正六年生弘历长女；1730年（八年）生次子永琏；1731年（九年）生弘历第三女。1735年10月8日（雍正十三年八月二十三日），即雍正帝去世当日，奉懿旨册为皇后。1738年1月23日（乾隆二年十二月初四），行册立礼。1746年5月27日（乾隆十一年四月初八）生皇七子永琮。1748年2月（乾隆十三年正月）随驾东巡，4月8日（三月十一日），崩于回銮途中的德州舟次，年三十七；19日（二十二日），帝亲定谥“孝贤”，6月16日（五月二十一日）行册谥礼。1752年12月2日（乾隆十七年十月二十七日）奉安胜水峪裕陵地宫。经嘉庆、道光两朝加谥，全谥“孝贤诚正敦穆仁惠徽恭康顺辅天昌圣纯皇后”。</p>
                <p style="text-indent: 2em;">2.继皇后（1718年3月11日—17676年8月）：</p>
                <p style="text-indent: 2em;">乌拉那拉·景娴，乾隆帝的第二任皇后，清史稿后妃列传中记其为皇后乌喇那拉氏。满洲镶黄旗，佐领那尔布的女儿，生于1718年3月11日（康熙五十七年二月初十），比乾隆小七岁。乾隆帝为亲王时，乌喇那拉氏为侧福晋。1737年（乾隆二年），封娴妃。1745年（乾隆十年），进娴贵妃。孝贤纯皇后逝世后，进皇贵妃，主持后宫。1748年3月11日（乾隆十五年），立为皇后。1752年（乾隆十七年），生皇十二子永璂，次年生皇五女，1755年（二十年）又生下皇十三子永璟。1765年（三十年）在随乾隆南巡，得罪乾隆帝。1766年8月（三十一年七月）崩，乾隆命丧葬仪式等同于皇贵妃。</p>
                <p style="text-indent: 2em;">3.孝仪纯皇后（1727年10月23日—1775年2月28日）：</p>
                <p style="text-indent: 2em;">魏佳·小玉，内管领清泰女，即《延禧攻略》里的“魏璎珞”的原型，1727年10月23日（雍正五年九月初九）生，比乾隆帝小十六岁。初入宫，1745年（乾隆十年）初封魏贵人，同年册为令嫔。1749年5月20日（乾隆十四年四月初五）晋令妃。1756年8月10日（乾隆二十一年七月十五日）生皇七女固伦和静公主，嫁拉旺多尔济；1757年（乾隆二十二年）生皇十四子永璐，殇；1759年1月5日（乾隆二十三年十二月初七）生皇九女和硕和恪公主，嫁札兰泰；1760年2月3日（乾隆二十四年十二月十八日）晋令贵妃；1760年11月13日（乾隆二十五年十月初六），生皇十五子嘉庆帝永琰，即位后改名颙琰；1766年（乾隆三十一年）生皇十七子庆僖亲王永璘，此时魏佳氏已经四十岁；1768年6月24日（乾隆三十三年五月初十）晋令皇贵妃。</p>
                <p style="text-indent: 2em;">1775年2月28日（乾隆四十年正月二十九）逝世逝，享年48岁，同年二月十一日谥为令懿皇贵妃，葬于清东陵裕陵地宫。1795年10月15日（乾隆六十年九月初三），乾隆帝宣示皇十五子永琰为皇太子，同时封其母令懿皇贵妃魏佳氏为皇后，册谥孝仪皇后；十月行孝仪皇后册谥礼。嘉庆、道光加谥累谥为“孝仪恭顺康裕慈仁端恪敏哲翼天毓圣纯皇后”，葬于裕陵。</p>
                <p style="text-indent: 2em;">
                    <strong>嘉庆皇后</strong></p>
                <p style="text-indent: 2em;">1.孝淑睿皇后（1760年10月2日—1797年3月5日）：</p>
                <p style="text-indent: 2em;">喜塔腊·青瑜，总管内务府大臣、副都统、承恩公和尔经额女，生于乾隆二十五年八月二十四日辰时，和嘉庆皇帝是同年生，但比嘉庆皇帝大42天。1774年6月5日（乾隆三十九年四月二十七日），高宗亲赐封为皇子永琰的嫡福晋，年十五岁。1780年（乾隆四十五年），生皇二女。1782年9月16日（乾隆四十七年八月初十），生皇二子绵宁（即道光帝）。1784年（乾隆四十九年），生皇四女固伦庄静公主。1796年2月（嘉庆元年正月），册立为皇后。</p>
                <p style="text-indent: 2em;">1797年3月5日（嘉庆二年二月初七），病逝，是清朝历史上最短命的皇后。1797年6月（嘉庆二年五月），乾隆谥孝淑皇后。1803年11月（嘉庆八年十月），入葬昌陵地宫。道光元年三月，加谥：孝淑端和仁庄慈懿光天佑圣睿皇后。道光三十年四月，加谥：孝淑端和仁庄慈懿敦裕光天佑圣睿皇后。咸丰十一年十月，加谥：孝淑端和仁庄慈懿敦裕昭肃光天佑圣睿皇后。</p>
                <p style="text-indent: 2em;">2.孝和睿皇后（1776年—1849年11月24日）：</p>
                <p style="text-indent: 2em;">钮祜禄·绮雪，礼部尚书恭阿拉女。后事仁宗潜邸，为侧室福晋。1793年7月（乾隆五十八年六月），生皇七女。1795年7月（乾隆六十年六月），生皇三子绵恺。嘉庆元年二月，仁宗即位，册封为和妃。1797年（嘉庆嘉庆二年），四月孝淑睿皇后崩，五月，太上皇敕谕继位中宫，先封为皇贵妃，1798年8月（嘉庆三年七月），晋封为和贵妃。嘉庆四年，立为皇后。1801年（嘉庆六年四月），补办册后仪式。</p>
                <p style="text-indent: 2em;">1805年（嘉庆十年二月），生皇四子瑞亲王绵忻。1820年（嘉庆二十五年八月），仁宗幸热河崩，皇后懿旨让皇二子绵宁继位，宣宗尊为皇太后，居寿康宫。1849年11月24日（道光二十八年十月初十）逝世，年七十三。宣宗春秋已高，方有疾，居丧哀毁，三十年正月，崩於慎德堂丧次。咸丰三年，葬后昌陵之西，曰昌西陵。初尊皇太后，上徽号。国有庆，累加上，曰恭慈康豫安成庄惠寿禧崇祺皇太后。逮崩，上谥。咸丰间加谥，曰孝和恭慈康豫安成钦顺仁正应天熙圣睿皇后。</p>
                <p style="text-indent: 2em;">
                    <strong>道光皇后</strong></p>
                <p style="text-indent: 2em;">1.孝穆成皇后（1796年—1808年2月27日）：</p>
                <p style="text-indent: 2em;">钮祜禄·玲儿，户部尚书、一等子布颜达赉女。道光帝为皇子时，1796年（嘉庆元年）十一月奉文综赐册，立为皇子绵宁的嫡福晋。1808年2月27日（嘉庆十三年正月二十一日）逝世，暂安于王佐村园寝。1820年（嘉庆二十五年）嘉庆即位，追谥为孝穆皇后。1827年（道光七年九月）殡宫自王佐村移葬，11月10日（九月二十二日）正式入葬东陵宝华峪万年吉地。1828年（道光八年九月），宝华峪万年吉地地宫渗水，1829年（道光九年五月），将孝穆皇后梓宫由地宫中启出，暂安于宝华峪正殿。1836年1月（道光十五年十二月），孝穆皇后梓宫迁葬入西陵境内的龙泉峪万年吉地地宫。1850年10月（道光三十年九月），加上尊谥：孝穆温厚庄肃端诚孚天裕圣成皇后。1861年（咸丰十一年）穆宗加上“恪惠”二字，1875年（光绪元年六月）加上尊谥“宽钦”二字。全谥为孝穆温厚庄肃端诚恪惠宽钦孚天裕圣成皇后。</p>
                <p style="text-indent: 2em;">2.孝慎成皇后（1790—1833年）：</p>
                <p style="text-indent: 2em;">佟佳·玉琦，满洲镶黄旗人，世袭三等承恩公、追封一等公舒明阿女，康熙年间的一等公佟图赖的后代。</p>
                <p style="text-indent: 2em;">1808年（嘉庆十三年），绵宁嫡妻钮祜禄氏逝世。钮祜禄氏死后，佟佳氏被嘉庆指为皇子绵宁的继室福晋。1813年7月29日（嘉庆十八年七月初三日），佟佳氏生绵宁长女。两月后，皇子绵宁被封为“智亲王”，佟佳氏也成了亲王福晋。1819年12月7日（嘉庆二十四年十月二十日），长女患病不治而去世，虚龄七岁，嘉庆帝特意下旨追封绵宁长女为郡主。第二年七月，智亲王绵宁即位为帝，改名为旻宁。</p>
                <p style="text-indent: 2em;">道光帝即位不久，追封其女为“端悯固伦公主”，并在为自己修建第一座帝陵时，在东陵许家峪为女儿选址建墓。1821年11月24日（道光元年十月三十日），晋封其兄裕宽为一等承恩侯。1822年12月（道光二年十一月），道光帝册立佟佳氏为皇后，1833年6月16日（十三年四月二十九），皇后逝世。道光帝在佟佳氏死后传旨要求王以下有顶戴者百日内均不得剃发，且均要停宴止乐一年。道光七月上谥为孝慎皇后，1836年1月28日（十五年十二月十一）与孝穆皇后钮祜禄氏一起葬入龙泉峪地宫。经过咸丰、同治、光绪三代累次上谥，谥号全称为：孝慎敏肃哲顺和懿诚惠敦恪熙天诒圣成皇后。</p>
                <p style="text-indent: 2em;">3.孝全成皇后（1808年3月24日—1840年2月13日）：</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/2a6e1-2933.jpg" /></p>
                <p style="text-indent: 2em;">钮祜禄·绣心，生于嘉庆十三年二月廿八日（1808年3月24日），属于历任外戚的镶黄旗钮祜禄氏家族（清朝初年五大臣额亦都之后），曾祖父为乾隆朝著名驻藏将军成德，祖父为战功赫赫的将军穆克登布，其曾祖和祖父皆为清朝功勋卓著声名显赫的著名将领，其父颐龄当时是乾清门侍卫，世袭二等男爵。父亲为清朝苏州驻防将军、乾清门二等侍卫、世袭二等男爵、赠一等承恩侯、晋赠三等承恩公颐龄。她是清宣宗道光皇帝爱新觉罗·旻宁的第三位嫡妻，即位后所立的第二位皇后。享年33岁，生奕詝。</p>
                <p style="text-indent: 2em;">钮祜禄氏不管生前死后都极受道光帝的宠爱及重视，从全贵人开始（实际册封时未册为全贵人而直接进位全嫔）、3个月后升全妃、一年后以17岁稚龄超越所有老资格嫔妃成为仅次于孝慎成皇后的全贵妃、而后皇贵妃，直至母仪天下的皇后。她于盛年暴崩，死因不明，至今众说纷纭，成为清宫疑案之一。她生前完全不用凭借子嗣的晋封速度堪称清宫罕见，当皇后后铁腕治后宫也说明了道光帝对其非比寻常的爱重支持，她死后其子奕詝（后来的咸丰帝）能顺利击败其他皇子最终继承大统，很大程度上是由于道光对她爱屋及乌。也因为孝全成皇后与道光帝感情极深，自她去世后道光便未曾再立皇后。</p>
                <p style="text-indent: 2em;">4.孝静成皇后（1812年—1855年）：</p>
                <p style="text-indent: 2em;">博尔济吉特·雨宁，生于1812年6月19日（嘉庆十七年五月十一日），刑部员外郎花郎阿之女。原为蒙古科尔沁左右翼（在今中国内蒙古通辽市境内）人，死后抬入满洲正黄旗。初入宫为静贵人，十四年为贵妃，二十年为静皇贵妃。文宗即位尊为皇考康慈皇贵太妃。1855年（咸丰五年七月）太妃病笃，尊为康慈皇太后，8月21日（七月初九日）崩，享年四十四岁。</p>
                <p style="text-indent: 2em;">咸丰年间初谥为：孝静康慈弼天抚圣皇后，不系宣宗谥，不祔庙。穆宗即位后，祔庙、加谥。尊谥为：孝静康慈懿昭端惠庄仁和慎弼天抚圣成皇后。有三子一女，分别为奕纲、奕继（均早殇）、皇六子恭亲王奕訢。皇六女固伦寿恩公主，道光二十五年四月下嫁景寿。孝静成皇后是清文宗咸丰皇帝爱新觉罗·奕詝的养母，虽然生前未成为皇后，但死后被咸丰帝追封为皇后，成为清朝历史上和顺治董鄂妃一样既非前朝皇后、也非本朝皇帝的生母而被追封的皇后。</p>
                <p style="text-indent: 2em;">
                    <strong>咸丰皇后</strong></p>
                <p style="text-indent: 2em;">1.孝德显皇后（1828年－1858年）：</p>
                <p style="text-indent: 2em;">萨克达氏，太常寺少卿富泰之女，镶黄旗满洲人。1828年（道光八年）出生，比咸丰帝大四岁。1846年（道光二十六年）进宫，由道光帝替咸丰帝册立萨克达氏为嫡福晋。1850年1月24日（二十九年十二月十二日逝世），无子女。清文宗咸丰帝即位后，追册谥曰孝德皇后。权攒田村，同治初，移静安庄，没过多久葬清东陵定陵，上谥。之后经过同治、光绪、宣统三朝皇帝的加谥之后，她正式的谥号是孝德温惠诚顺慈庄恪慎徽懿恭天赞圣显皇后。</p>
                <p style="text-indent: 2em;">2.孝贞显皇后（1837-1881年）：即慈安皇太后</p>
                <p style="text-indent: 2em;">慈安皇太后，俗称东太后，简称慈安，名为钮祜禄·瑞芬，满洲镶黄旗人，广西右江道三等承恩公穆扬阿之女，比咸丰小6岁。于1852年（咸丰二年）被选秀入宫，封贞嫔，贞妃，五月晋贞贵妃，六月奉旨立为皇后，时年16岁。1860年8月，咸丰皇帝在热河病死，年仅6岁的儿子载淳继位，慈禧与恭亲王奕訢定计，发动祺祥政变（辛酉政变），处死了肃顺等辅政八大臣，慈安和慈禧两太后垂帘听政，执掌国家最高权力。1881年4月8日（光绪七年三月初十）戌时，年仅四十五岁的慈安皇太后猝然薨于钟粹宫。五月，上尊谥曰孝贞慈安裕庆和敬仪天祚圣显皇后。九月，葬定陵东之普祥峪，曰定东陵，升祔太庙。宣统元年四月，加上尊谥诚靖二字，孝贞慈安裕庆和敬诚靖仪天祚圣显皇后。</p>
