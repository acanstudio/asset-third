                <p style="text-indent: 2em;">【通古斯民族男子用野兽皮取名的现象很常见。成亲王永瑆后人、满族语言学家金启孮说过，努尔哈赤的意思是“野猪皮”，其弟舒尔哈齐名字意为“小野猪皮”，穆尔哈齐意为“老羊皮”，雅尔哈齐意为“豹子皮”】</p>
                <p style="text-indent: 2em;">
                    <strong>太宗文皇帝</strong>&#160;（taidzung genggiyen xu hvwangdi）</p>
                <p style="text-indent: 2em;">皇太极 Hvwang taiji 【Hvwang taiji汉译清初写作“黄台吉”或“洪太时”，乾隆钦定译成“皇太极”。皇太极本名黑还勃烈，黑还=黄，勃烈=狼（蒙古语），也有学者认为勃烈=贝勒。皇太极年轻叫黄贝勒，长大叫黄台吉（台吉是蒙古贵族封号，相当于王子）】</p>
                <p style="text-indent: 2em;">汗号天聪 abkai sure abkai=天，sure=聪明、聪睿</p>
                <p style="text-indent: 2em;">汗号崇德 wesihun erdemungge【wesihun=向上，erdemungge=有德的、有技术的、有才能的】</p>
                <p style="text-indent: 2em;">【首位大清国（daicin gurun）皇帝（hūwangdi）】</p>
                <p style="text-indent: 2em;">成宗义皇帝（jurgangga hūwangdi）</p>
                <p style="text-indent: 2em;">多尔衮（dorgon）（顺治追尊，
                    <a href="http://www.xixik.com/content/753aa6d375ef9679">后裭夺</a>）</p>
                <p style="text-indent: 2em;">【谥号全称是懋德修道广业定功安民立政诚敬义皇帝，erdemu be mutebufi,doro be dasaha, gurun be badarambufi, gung be mutebuhe, irgen be elhe obuha, dasan be ilibuha, akdun ginggun, jurgangga hūwangdi】</p>
                <p style="text-indent: 2em;">
                    <strong>世祖章皇帝</strong>&#160;（xidzu eldembuhe hvwangdi）</p>
                <p style="text-indent: 2em;">福临 Fu lin</p>
                <p style="text-indent: 2em;">年号顺治 ijishuun dasan【ijishuun=顺应，dasan=政治、统治】</p>
                <p style="text-indent: 2em;">
                    <strong>圣祖仁皇帝</strong>&#160;（xengdzu gosin hvwangdi）</p>
                <p style="text-indent: 2em;">玄烨 Hio wan</p>
                <p style="text-indent: 2em;">年号康熙 elhe taifin【elhe=安、平安、慢、缓慢，taifin=太平】</p>
                <p style="text-indent: 2em;">（在尼布楚条约中写作“中国大圣皇帝”dulimbai gurun-i enduringge hūwangdi）</p>
                <p style="text-indent: 2em;">
                    <strong>世宗宪皇帝</strong>&#160;（xidzung temgetulehe hvwangdi）</p>
                <p style="text-indent: 2em;">胤祯 In jen</p>
                <p style="text-indent: 2em;">年号雍正 huuwaliyasun tob【huuwaliyasun=和睦、和谐，tob=公正、正直】</p>
                <p style="text-indent: 2em;">
                    <strong>高宗纯皇帝</strong>&#160;（gaodzung yongkiyangga hvwangdi）</p>
                <p style="text-indent: 2em;">弘历 Hung li</p>
                <p style="text-indent: 2em;">年号乾隆 abkai wehiyehe【abkai=天，wehiyehe=隆、顶】</p>
                <p style="text-indent: 2em;">
                    <strong>仁宗睿皇帝</strong>&#160;（zindzung sunggiyen hvwangdi）</p>
                <p style="text-indent: 2em;">颙琰 Yung yen</p>
                <p style="text-indent: 2em;">年号嘉庆 saicungga fengshen【saicungga=值得夸奖的，fengshen=福裕、福运】</p>
                <p style="text-indent: 2em;">
                    <strong>宣宗成皇帝</strong>&#160;（siowandzung xanggan hvwangdi）</p>
                <p style="text-indent: 2em;">旻宁 Min ning</p>
                <p style="text-indent: 2em;">年号道光 doro eldengge【doro=道、理、礼，eldengge=光明的、光亮的、光线强烈的】</p>
                <p style="text-indent: 2em;">
                    <strong>文宗显皇帝</strong>&#160;（wendzung iletu hvwangdi）</p>
                <p style="text-indent: 2em;">奕詝 I ju</p>
                <p style="text-indent: 2em;">年号咸丰 gubci elgiyengge【gubci=全的、全部的，elgiyengge=丰裕的】</p>
                <p style="text-indent: 2em;">
                    <strong>穆宗毅皇帝</strong>&#160;（mudzung filingga hvwangdi）</p>
                <p style="text-indent: 2em;">载淳 Dzai xvn</p>
                <p style="text-indent: 2em;">年号祺祥/同治&#160;yooningga dasan【yooningga=全部的，dasan=政治、统治】</p>
                <p style="text-indent: 2em;">
                    <strong>德宗景皇帝</strong>&#160;（dedzung ambalinggv hvwangdi）</p>
                <p style="text-indent: 2em;">载湉 Dzai tiyan</p>
                <p style="text-indent: 2em;">年号光绪 badarangga doro【badarangga=发展的，doro=道、理、礼】</p>
                <p style="text-indent: 2em;">
                    <strong>宣统皇帝/末帝</strong>（无庙号）</p>
                <p style="text-indent: 2em;">溥仪（pu i）</p>
                <p style="text-indent: 2em;">宣统 gehungge yoso【gehungge=光亮的、光线柔和的、气质好的，yoso=道德】</p>
                <p style="text-indent: 2em;">
                    <strong>清朝皇帝之最&#160;</strong></p>
                <p style="text-indent: 2em;">清朝共传十二帝，在这十二位皇帝中，每一位都有各自的专属纪录。</p>
                <p style="text-indent: 2em;">
                    <strong>清太祖努尔哈赤：清朝最善战的皇帝</strong></p>
                <p style="text-indent: 2em;">努尔哈赤早年仅凭祖父和父亲留下的十三副遗甲起兵，队伍最初规模只有数十人。但令人感到不可思议的是，努尔哈赤竟然就靠这点“本钱”征战沙场，从胜利走向胜利，队伍规模如同雪球一样越滚越大。1616年，努尔哈赤在赫图阿拉建立后金政权。三年后，努尔哈赤又率领六万八旗军在“萨尔浒之战”中击溃了明朝的十一万精锐部队，从而奠定了后金在关外地区的霸主地位。</p>
                <p style="text-indent: 2em;">
                    <strong>清太宗皇太极：清朝最杰出的皇帝</strong></p>
                <p style="text-indent: 2em;">皇太极是努尔哈赤第八子，他文武双全，从少年时期开始就随努尔哈赤南征北战。努尔哈赤去世后，皇太极在众贝勒的推荐下袭承汗位。他即位后对后金内部展开大刀阔斧的改革，其中最突出的成就在于实施满汉一体的政策，从而争取到了许多汉人对后金政权的支持。在军事方面，皇太极在位期间征服了朝鲜、漠南蒙古，并在“松锦之战”中歼灭了明朝最后的精锐力量，为后来的清朝入主中原打下了坚实基础。</p>
                <p style="text-indent: 2em;">1636年，皇太极在盛京（今沈阳）称帝，并将国号由“大金”改为“大清”，此举意味着皇太极志在入主中原，与明朝争夺天下。然而就在明朝灭亡前夕，皇太极却突然猝死，终年52岁。</p>
                <p style="text-indent: 2em;">
                    <strong>清世祖福临（顺治）：清朝最痴情的皇帝</strong></p>
                <p style="text-indent: 2em;">在清朝所有皇帝中，要论谁最痴情，恐怕顺治皇帝要排在第一位。顺治帝和董鄂妃的爱情故事在历史上相当出名，后世也借这个题材衍生出了许多小说和电视剧。董鄂妃从选秀入宫到被封为皇贵妃，期间只有短短数月时间，这种极为罕见的晋升速度也从侧面说明了董鄂妃在顺治帝心目中的地位。然而天妒红颜，董鄂妃在22岁的时候就香消玉殒了，年轻的顺治帝悲痛欲绝，身体也彻底垮掉了。1661年2月5日，顺治帝驾崩，年仅24岁。此时离董鄂妃去世还不到半年时间，“问世间情为何物，直教人生死相许”。</p>
                <p style="text-indent: 2em;">
                    <strong>清圣祖玄烨（康熙）：清朝在位时间最长的皇帝</strong></p>
                <p style="text-indent: 2em;">康熙皇帝是中国历史上知名度最高的皇帝之一，他一生经历了擒鳌拜、平三藩、收台湾、亲征准噶尔等重大历史事件，开创了“康乾盛世”的大好局面。从8岁登基，到69岁驾崩，康熙皇帝共在位61年，他不仅是清朝在位时间最长的皇帝，同时也是中国历史上在位时间最长的皇帝。</p>
                <p style="text-indent: 2em;">
                    <strong>清世宗胤禛（雍正）：清朝最劳碌的皇帝</strong></p>
                <p style="text-indent: 2em;">如果要在中国历史上的帝王中选出个“劳模”，雍正皇帝很可能会高票当选。他在位十三年，几乎天天凌晨4点起床，一直工作到深夜才睡觉，每年唯有自己生日才会休息一天。雍正帝一生在大臣奏折上的批语达到了惊人的1000多万字，有些批语甚至比奏折本身的内容还长。所以后世在谈及雍正帝死因的时候，许多人认为他就是活活给累死的。</p>
                <p style="text-indent: 2em;">
                    <strong>清高宗弘历（乾隆）：清朝最长寿的皇帝</strong></p>
                <p style="text-indent: 2em;">在清朝的十二位皇帝中，乾隆帝无疑是最幸运的那一个。他在25岁时继承皇位，获得最高权力的过程毫无悬念；他承袭康熙、雍正两朝打下的坚实基础，统治着一个欣欣向荣的帝国；他身体健康，从来没有遇到大病大灾。所以乾隆一生可以说是享受到了人世间能享受的一切美好事物。1799年2月7日，乾隆皇帝在紫禁城养心殿去世（
                    <a href="http://www.xixik.com/content/c98205091e18d114" target="_blank" title="1799年乾隆与华盛顿去世，世界到底有多疯狂？ ">1799年乾隆与华盛顿去世，世界到底有多疯狂？</a>），享年89岁。乾隆不仅是清朝最长寿的皇帝，同时也是中国历史上最长寿的皇帝（南越武帝赵佗103岁存疑）。</p>
                <p style="text-indent: 2em;">
                    <strong>清仁宗颙琰（嘉庆）：清朝最平庸的皇帝</strong></p>
                <p style="text-indent: 2em;">嘉庆皇帝当属清朝最平庸的皇帝。他既没有政治胆略又缺乏革新精神，一生唯一值得称道的成就便是惩治了大贪官和珅。嘉庆帝执政时期，外部世界正发生着翻天覆地的变化。然而清朝在这位平庸天子的治理下，依然保持着强烈的农耕色彩，大清帝国就这样不可挽回地走向了衰落。</p>
                <p style="text-indent: 2em;">
                    <strong>清宣宗旻宁（道光）：清朝最抠门的皇帝</strong></p>
                <p style="text-indent: 2em;">
                    <a href="http://www.xixik.com/content/4bc7be3729c46f24" title="节俭过了头的道光皇帝 却耗巨资修皇陵沽名钓誉">道光皇帝不仅是清朝历史上最抠门的皇帝</a>，甚至可以说是中国历史上最抠门的皇帝。清朝历代皇帝在饮食方面非常铺张浪费，比如乾隆皇帝每餐都要上一百多个菜。可是道光帝的餐桌上只有四道菜，而且还是以素菜为主。所以在清代帝王画像上，道光皇帝面容消瘦是有原因的。有一次皇后生日，道光帝决定宴请群臣给皇后庆生。然而当众人兴高采烈等着开席时却发现每人只有一碗白菜长寿面，上面撒了零零星星的一点肉末。清朝最抠皇帝，道光绝对当之无愧。</p>
                <p style="text-indent: 2em;">
                    <strong>清文宗奕詝（咸丰）：清朝最倒霉的皇帝</strong></p>
                <p style="text-indent: 2em;">咸丰帝的才能在清朝所有帝王中大概位居中游偏上，他在位期间勤于政务，努力革除积弊，希望重现大清帝国昔日的雄风。然而事与愿违，咸丰帝却成了清朝最倒霉的皇帝。太平天国运动、英法联军攻入北京火烧圆明园这些重大历史事件都是在他任期内发生的。更为不幸的是，咸丰帝只有一个儿子，而这个儿子的母亲便是后来大名鼎鼎的慈禧太后。</p>
                <p style="text-indent: 2em;">
                    <strong>清穆宗载淳（同治）：清朝最短命的皇帝</strong></p>
                <p style="text-indent: 2em;">同治皇帝在位13年，与历史上那些帝王的在位时间比起来也不算短。然而他却是清朝最短命的皇帝，终年只有19岁。同治帝在位期间，朝政大权都掌握在两宫皇太后和恭亲王奕訢手中，所以同治帝短暂的一生基本没有什么作为。</p>
                <p style="text-indent: 2em;">
                    <strong>清德宗载湉（光绪）：清朝最惨的皇帝</strong></p>
                <p style="text-indent: 2em;">如果要在清朝十二位帝王中评选出谁是最惨皇帝，光绪肯定排名第一。光绪帝一生基本都活在慈禧太后的阴影之中，从头到尾都是受慈禧太后控制的“提线木偶”。在其亲政期间，甲午之战中他力主反击，大力支持维新变法，但因变法失败而遭幽禁，直至去世。然而比成为傀儡更惨的是，光绪还是清朝唯一一个被毒死的皇帝，死时年仅38岁。</p>
                <p style="text-indent: 2em;">
                    <strong>宣统皇帝溥仪：清朝末代皇帝</strong></p>
                <p style="text-indent: 2em;">末代皇帝溥仪，清朝最后一个皇帝，也是中国历史上最后一个皇帝，也是清朝在位时间最短的一位皇帝。1911年2月12日，
                    <a href="http://www.xixik.com/content/6fd3be3f2189c843" title="1912年2月12日 清帝退位中国再也没有皇帝了">在辛亥革命的浪潮中退位</a>。此后，以逊帝身份居住在皇宫。1924年11月5日，
                    <a href="http://www.xixik.com/content/1b3f6bfcebca81f0" title="亲历者揭秘冯玉祥驱逐末代皇帝溥仪的经过">被冯玉祥驱逐出宫</a>。后又成为了满洲国的皇帝，又成为了新政权的战犯，又成为了新社会的公民。1967年10月17日在北京逝世，享年61岁。</p></div>
