            <div class='content_text'>
                <p style="text-indent: 2em;">皇帝作为中国古代封建社会至高无上的统治者，一直是几千年来人们茶余饭后闲聊的对象。而在中国历史上这么多的皇帝里面，秦皇汉武，唐宗宋祖，明太祖朱元璋等开国皇帝比较了解。也罢，毕竟这些人都是皇帝里面的劳模。然而历朝历代君王不免良莠不齐，每个
                    <a href="http://m.114.xixik.com/chaodai/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="历史朝代顺序表">朝代</a>总会出那么几个昏君，导致外敌入侵，生灵涂炭，江山断送他人之手。像什么晋惠帝，唐僖宗，宋徽宗，明武宗啊，昏庸无能，遗臭万年。而纵观
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>十二帝，虽说能力有别，却没有一个真正的昏君，不仅一个历史学家说。的确，清朝的没有荒淫无道、常年不上朝的皇帝。
                    <a href="http://m.114.xixik.com/chinese-emperors/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝皇帝">清朝皇帝</a>个个勤勉：康熙累吐血，雍正工作狂。但是笔者想说，这些皇帝特别是雍正之后的皇帝们，还不如不勤政，因为勤政的无能君主，远比不上朝的昏君危害大。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>也有其他王朝所难以企及的地方，
                    <a href="http://m.114.xixik.com/chinese-emperors/qing/" title="清朝历代皇帝简介">这就是整整十代帝王</a>，如果加上关外的两代就是十二代，
                    <a href="http://www.xixik.com/content/7a00b930eba47005" title="中国历史上十大昏君 亡国君未必是昏君">没有真正的昏君</a>，
                    <a href="http://www.xixik.com/content/9ce1ab7d9b6c435a" title="清朝皇帝很勤政但是治理却很烂 看他们日常作息">几乎所有的皇帝都比较勤政</a>，至少都怕落个荒殆朝政的恶名，“
                    <a href="http://www.xixik.com/content/156138cca3206c48" title="万历皇帝三十年不上朝？但并不代表不管事怠政">从此君王不早朝</a>”的事，谁也不敢做。这一点，跟相邻的
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="明朝">明朝</a>形成了鲜明的对照。而明清之际，
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>能做到这一点，的确是相当地不容易。我们知道，明代以前，大多数
                    <a href="http://m.114.xixik.com/chaodai/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="历史朝代顺序表">朝代</a>都有宰相，王朝的行政事务是由宰相主持行政系统来处理的，
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>虽然握有国家大事的最后决定权，但他更多的是“国家元首”而非“政府首脑”。然而，明代自朱元璋之后，相位被废，从此
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>兼宰相之任，虽然有人帮忙（明代的内阁，清代的军机处都等于是
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>的幕僚班子），但一不留神还是会被事务和公文堆给活埋了。吃不得辛苦的朱家子孙，干脆将批阅奏章的大权交给了原本只是伺候
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>笔墨的“秉笔太监”，甚至还有像万历
                    <a href="http://m.114.xixik.com/chinese-emperors/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="中国皇帝列表">皇帝</a>一样，就是不理朝政，将所有的大事包括任免官员全部搁置，天天睡大觉。比起明代那些天天睡懒觉、炼丹、做木匠活和游龙戏凤封自己为“总兵”的
                    <a href="http://www.xixik.com/content/840548b8cada6d60" title="明朝奇葩皇帝">明朝奇葩皇帝</a>，显然那些起早贪黑的爱新觉罗子孙要招人爱得多。相比较而言，康熙和雍正两位皇帝勤于政事，因此加深了人们的好印象。</p>
                <p style="text-indent: 2em;">然而，清朝皇帝的勤政恰恰是其政权体系不完善的表现。清朝是中国封建王朝中央集权发展的巅峰时期，中央集权本身不可怕，可怕的是这些权名义上在中央，实际上在皇帝一个人手中，皇帝不放权，当然大大小小的事都要找他了，这点清朝就不如明朝，别看万历皇帝几十年不上朝，但是大明帝国一点没瘫痪。
                    <strong>所以清朝皇帝根本不是勤政，他们是在用最笨的方法管理国家，这是历史的倒退。</strong>在清朝之前，明朝有成熟的内阁制度，在这之前的王朝还有
                    <a href="http://www.xixik.com/content/65913debd98f7311" title="中国帝王为何对丞相“又爱又恨”？">成熟的宰相制度</a>，这些清朝皇帝都不学，偏偏撸起袖子自己干，活该挨累。其次，清朝的皇帝虽然很勤劳，但是都没用到正地方，这和学习一个道理，别看某个学生下课学习、熬夜学习，但他只是看起来很努力罢了，根本不是学习，只是在座位上坐了十几个小时罢了。清朝皇帝讨厌就讨厌在自己没什么能力（对比一下同时期的康熙、俄国的彼得大帝就明白了），还非要大权独揽，想大权独揽那就只能勤政了。试想，如果皇帝有自知之明，知道自己没能力，而放权让有能力的大臣管事，那才是社稷之福。但是，其他王朝的皇帝敢这么干，清朝皇帝根本不敢这样放权。</p>
                <p style="text-indent: 2em;">由于
                    <a href="http://www.xixik.com/content/1845b5a61dd3c445">满清是以异族身份捡漏入主中原</a>，清朝皇帝很大的精力用于防止汉人。第二次鸦片战争，外国军队都打进首都了，清朝统治者也不敢举国一战，打不过就赶紧求和，反观太平天国运动时期，清朝统治者即使付出再大的代价也要予以剿灭，同样是打仗，清军面对外国军队只会冲锋，毫无战术，而面对太平天国的洋枪洋炮时，就会挖地道、夜间偷袭、修建坚固的营寨了。而面对外国军队，清军士兵一个个那么笨，等到面对本国人时，一个个就会打仗了，而且凶悍无比，这种内战内行、外战外行的态度，实在让人鄙视，这真是异族政权。</p>
                <p style="text-indent: 2em;">元和清都是少数
                    <a href="http://m.114.xixik.com/minority/" style="color: rgb(0, 0, 0);" title="民族">民族</a>王朝，但君王之道差别甚大，
                    <a href="http://m.114.xixik.com/chaodai/yuan/" style="color: rgb(0, 0, 0);" title="元朝">元朝</a>的皇帝和蒙古贵族几乎全不懂汉语，对汉文化并不推崇，最终称霸中原不足百年就被赶回蒙古草原了。当然也是因为元的统治阶层不懂汉语，民间文学创作发展比较好，像《窦娥冤》这种题材也不会被打压（看不懂，也就没什么文字狱，哈哈）。而
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇家则非常重视教育，皇族子弟均精通满汉蒙三种语言，皇帝遗诏都是用三种语言写成，可想而知为此付出多少努力。</p>
                <p style="text-indent: 2em;">古代皇帝住在深宫大院，除了后宫嫔妃和王公大臣，其余人很难见到一面，更别说知道他的日常作息。清朝入关之前，以及入关之初，由于战争频繁，不太可能对皇帝的日常言行进行详细的记载。到了康熙时期，政权基本稳定，康熙皇帝很重视御门听政，每天都在乾清门听取各部院大臣奏事或与内阁大臣商决朝政。君臣之间的问答、讨论，均被记注官进行记载。自康熙皇帝以后，到宣统皇帝为止，在长达239年时间里，清朝一共留下12000多册起居注册。我们可以通过这些珍贵的资料，了解
                    <a href="http://www.xixik.com/content/19d57342db713bfb" style="text-indent: 2em;" title="清朝皇帝的日常作息">清朝皇帝的日常作息</a>。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇帝相对都比较勤奋，勤勉的一个关键因素，就是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>的一项制度。清朝皇族自诩以弓马夺天下，皇子在学习文化知识以外，还要学习武术，像骑马射箭啊，那是得样样精通的。还在关外的时候，多尔衮就给5岁的顺治小皇帝请了5个师傅，3个满人，2个汉人。清朝皇家教育到康熙“平定三藩”后才确立正规制度，据《养吉斋丛录》记载：“我朝家法，皇子、皇孙六岁，即就外傅读书。”意思是皇子虚岁满6岁便开始读书，早5点到下午3点，共10小时。一年只能休息5天，即元旦、端午、中秋、皇帝生日、自己生日，此外连除夕都不能放假。在这样的教育模式下，清朝皇子们个个都是应试的精英。</p>
                <p style="text-indent: 2em;">虽然是凌晨5点开始上课，但皇子需凌晨4点到书房复习前天的功课。也就是说上课时间比皇帝上早朝还要早。“幼年读书必以一百二十遍为率，盖不如此则义理不能淹通” 意思是必须一本书，朗诵120遍，才可以知道其中道理。而且，每年，所有皇子还要随同皇帝和八旗禁卫军去木兰围场，参加狩猎，骑射训练。学习鸟枪，火炮，弓马骑射等等军事知识。夏天不准摇扇子，午饭以后立刻学习没有休息时间。</p>
                <p style="text-indent: 2em;">康熙少年的时候，五更即起读书，“竟至过劳，痰中带血，亦未少辍”。所以康熙四书五经能倒背如流，而乾隆皇帝14岁就能做到这一点，故深得康熙喜爱。</p>
                <p style="text-indent: 2em;">清盛期皇帝几乎每天都会去上书房巡视，康熙尤勤，每天9点左右到上书房听皇子们背书，有时下午四五点钟还会再来一趟，带着大臣们一起听皇子背书。皇子们直到结婚分府后，方能免除此番苦役。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇帝勤勉体现在方方面面，比如早朝，一般是10天一朝，而有些不理政务的皇帝，像
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="color: rgb(0, 0, 0);" title="明朝">明朝</a>的
                    <a href="http://www.xixik.com/content/156138cca3206c48" title="万历皇帝不上朝但并不代表不管事怠政   ">万历皇帝创下28年不早朝的记录</a>，而
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇帝几乎天天早朝，而且是每天五更天（5点到7点）。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>乃至
                    <a href="http://m.114.xixik.com/chinese-dynasties/" style="color: rgb(0, 0, 0);" title="中国历史">中国历史</a>上最勤勉的皇帝是雍正。史书记载，雍正只在自己的生日那天才会休息，在位13年来，他每天的睡眠时间不足4个小时，在数万件奏折上写下的批语多达1000多万字。在中国封建皇帝中像雍正这样勤奋的十分罕见。</p>
                <p style="text-indent: 2em;">
                    <strong>
                        <a href="http://www.xixik.com/content/19d57342db713bfb" target="_blank" title="如果你的大清没亡 那皇帝的一天是怎么过的？">如果你的大清没亡 那皇帝的一天是怎么过的？</a></strong>
                </p>
                <p style="text-indent: 2em;">清朝皇帝的一天到底是怎么度过呢？</p>
                <p style="text-indent: 2em;">04:00 AM</p>
                <p style="text-indent: 2em;">起床，洗漱，把辫子梳好，穿上朝服，先喝一碗羹汤，准备上早朝，听大臣们汇报今日头条。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/7481e-4825.jpg" /></p>
                <p style="text-indent: 2em;">这是皇上在养心殿的卧房。</p>
                <p style="text-indent: 2em;">08:00 AM</p>
                <p style="text-indent: 2em;">正式吃个早餐。太监会在这时把“膳牌”递给你，上面是请求召见的王公大臣们，由你自行决定饭后要约见的人。</p>
                <p style="text-indent: 2em;">吃完早餐，先到慈宁宫给太后请安。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/f3ee7-2333.jpg" /></p>
                <p style="text-indent: 2em;">这是慈禧太后所住的慈宁宫内景。</p>
                <p style="text-indent: 2em;">09:00 AM</p>
                <p style="text-indent: 2em;">正式开始工作前，每日必读几遍先皇留下的《实录》和《圣训》。所谓《圣训》，是前朝皇帝告诫臣下的诏令、言辞语录；《实录》是历代皇帝统治时期治国的编年大事记。</p>
                <p style="text-indent: 2em;">接着，开始批阅奏折，召见大臣，下发谕旨。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/e14d3-6395.jpg" style="width: 90%; max-width: 90%;" /></p>
                <p style="text-indent: 2em;">乾清宫——皇上的办公地点之一。</p>
                <p style="text-indent: 2em;">清朝皇帝日常处理政务有两种方式，一种是在乾清门御门听政；一种是在养心殿处理朝政。如前所述，康熙皇帝很重视御门听政，几乎每天都要前往乾清门听政，接受王公大臣的奏折，听取工作汇报，并作出了许多重大决策。康熙皇帝之后，历代皇帝逐渐放弃了御门听政，只在养心殿处理朝政。到了晚清，慈禧太后、慈安太后则选择在东暖阁“垂帘听政”。</p>
                <p style="text-indent: 2em;">13:00 PM</p>
                <p style="text-indent: 2em;">到了11点钟，皇帝放下工作，进入午休时间。到了吃晚饭的时间，没错，是晚饭。根据满洲习俗，当时每天只吃两顿饭，“早膳”和“晚膳”。</p>
                <p style="text-indent: 2em;">我们知道，中国自宋朝以后，已经实行一日三餐制。可清朝一直保留着一日两餐制，每天只吃两顿饭，皇帝也不例外。</p>
                <p style="text-indent: 2em;">俗话说：“孤家寡人。”皇帝用膳，是一个人进行的。没有皇帝的旨意，任何人不能跟皇帝一起用膳，连皇后都不行。在电视剧《延禧攻略》里，女主人公魏璎珞只是一名妃子，不但陪乾隆皇帝用膳，还大大咧咧地坐下来。这种荒诞的剧情在真实历史里，是绝对不可能发生的。</p>
                <p style="text-indent: 2em;">小太监会站在你边上陪你。在你吃前，他会先用银制的试毒牌看看有没有毒。</p>
                <p style="text-indent: 2em;">平常的菜单是：主菜八品、小菜四品，再加火锅、粥、汤等，多达几十种菜。</p>
                <p style="text-indent: 2em;">如果你有什么觉得特别好吃的，可一定不能多吃。小太监会及时帮你转到下一道菜，这都是为你好，防止有人在你喜欢吃的菜里下毒。</p>
                <p style="text-indent: 2em;">总之，在宫里还是小心为妙。</p>
                <p style="text-indent: 2em;">15:00 PM</p>
                <p style="text-indent: 2em;">吃完饭，睡个午觉，如果国事繁重就继续批奏折，如果想放松下，天气好可以放风筝，天气不好就欣赏下自己的荣华富贵，文物古玩之类的。</p>
                <p style="text-indent: 2em;">如果工作完毕，便可以进行适当的娱乐休闲活动。当然，在深宫大院，皇帝们能够选择的娱乐休闲活动也不多，充其量到畅音阁看看大戏，到御花园里瞅瞅花鸟鱼虫。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/12489-8318.jpg" /></p>
                <p style="text-indent: 2em;">在畅音阁听戏是历代皇帝们最喜欢的娱乐之一。</p>
                <p style="text-indent: 2em;">17:00 PM</p>
                <p style="text-indent: 2em;">到了晚上5点，皇帝如果感到饿了，可以吩咐御膳房加一顿点心。这些点心都是提前准备好的，只要皇帝一声“传膳”，御膳房太监在几分钟内便会将点心摆满膳桌。</p>
                <p style="text-indent: 2em;">当然，“抠门皇帝”道光皇帝是不允许将点心摆满膳桌的。为了尽可能地节约宫中开支，他平时用膳连肉食都“戒了”。到了夏天，宫中要吃西瓜解暑。道光皇帝觉得吃西瓜太浪费了，便在最热的三伏天命令太监：“明日取消西瓜，只供水。”道光皇帝经常挂在嘴边的一句话就是：“朕不能以口腹之故，枉费一钱！”</p>
                <p style="text-indent: 2em;">进完点心，皇帝要祭拜神灵。祭拜神灵的规定，是顺治皇帝制定的。为了方便皇帝在夜晚祭拜神灵，清廷在紫禁城里设立了40多座祭拜神灵的场所，分布于各处。</p>
                <p style="text-indent: 2em;">19:00 PM</p>
                <p style="text-indent: 2em;">进入“礼佛书经”时间，在你的起居卧榻边上有个小佛堂，专门用来上香，祭拜神灵。拜完后，准备沐浴更衣。</p>
                <p style="text-indent: 2em;">21：00 PM</p>
                <p style="text-indent: 2em;">
                    <a href="http://www.xixik.com/content/f94e87fdfeb2d91a" title="清朝皇帝临幸妃子全过程如同嫖妓">终于进入了喜闻乐见的翻绿牌行动</a>。</p>
                <p style="text-indent: 2em;">这部分工作主要由“敬事房”的小太监们负责，那是一个“专司皇帝交媾之事”的机构。</p>
                <p style="text-indent: 2em;">由于你不能到妃嫔宫里过夜（太危险了），所以贴心的小太监会把妃嫔的名字写在涂有绿颜色的小木牌上。</p>
                <p style="text-indent: 2em;">待你选完后，小太监会通知被选中的妃嫔洗干净、脱光，用棉被包裹好后，直接送到你的床上。除了皇后，所有的妃嫔都必须经历这道“工序”。</p>
                <p style="text-indent: 2em;">你在忙正事儿时，小太监也不能闲着，除了详细地记录当晚的情况，为了防止意外发生，他还会守在窗前，提醒你要适可而止。</p>
                <p style="text-indent: 2em;">完事之后，小太监会把妃嫔带到附近的宫殿，让你一个人孤枕而眠。</p>
                <p style="text-indent: 2em;">有比较勤奋的皇帝，还要在就寝前批阅奏折。康熙皇帝、雍正皇帝、乾隆皇帝从来不要别人代笔批阅奏折，所以他们往往批阅奏折到深夜。</p>
                <p style="text-indent: 2em;">雍正皇帝最爱批阅奏折，每篇奏折都要认真批示、回复，包括一些没有什么实际意义的“垃圾奏折”。有时候批阅的文字洋洋洒洒一大篇，甚至超过了奏折本身。遇到大臣在奏折里写了错别字，雍正皇帝还一本正经地修改过来。据统计，雍正皇帝在位12年，批阅奏折5万余件，每天要写1万多字，还是蛮辛苦的。</p>
                <p style="text-indent: 2em;">
                    <a href="http://www.xixik.com/content/19d57342db713bfb">这就是清朝皇帝普通的一天</a>。</p>
                <p style="text-indent: 2em;">其实真实的皇帝生活，更多的是节制而规律的，不仅不像电视剧里那么潇洒。</p>
                <p style="text-indent: 2em;">
                    <strong>
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>12位皇帝 三位勤勉最为突出</strong></p>
                <p style="text-indent: 2em;">1.清太宗皇太极。皇太极可以说是个工作狂，从登基到逝去，一生勤于政事，事必躬亲，导致积劳成疾，突然死去。史书记载，公元1643年，皇太极在沈阳故宫清宁宫东暖阁“端坐无疾而终”。仅从这点上看，皇太极也称得上是“千古一帝”了。</p>
                <p style="text-indent: 2em;">2. 清圣祖康熙帝，自除掉鳌拜亲政后，每日“昧爽视事，唯恐有怠政务，数十年如一日，励精图治”。正如他在遗诏中所说的那样:“自御极以来，孜孜汲汲，小心敬慎，夙夜不遑，未尝少懈。数十年来，殚心竭力。有如一日，此岂劳苦二字所能概括耶？”</p>
                <p style="text-indent: 2em;">3.清世宗雍正。据史料记载，雍正皇帝是最为勤政的一个。他每天只睡四个小时，真可谓“起的比鸡早，睡的比狗迟”，天天熬夜，以至于把眼睛都熬的近视了。他在位期间，自诩“以勤告天下”，不巡幸，不游猎，日理政事，终年不歇。雍正的确是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>所有皇帝中最懂得励精图治和珍惜皇帝这个职位的人，为什么这么说呢？首先，雍正皇帝，本来就不被康熙帝所看好，后来在众多皇子争夺皇位中胜出，被选来做皇帝。这就是康熙晚年九子夺嫡的胜利者。</p>
                <p style="text-indent: 2em;">雍正皇帝在坐上皇位以后，作了一系列重大的改革，他加强对少数
                    <a href="http://m.114.xixik.com/minority/" style="color: rgb(0, 0, 0);" title="民族">民族</a>的统治，并且大力整顿财政，最重要的是设立军机处。为了改变
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>晚立储的制度，他又改善秘密立储制度，这样使皇位在继承的时候更加制度化。同时也改变了父亲康熙皇帝在晚年建立储时，皇子骨肉相残的局面出现。</p>
                <p style="text-indent: 2em;">雍正帝是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>最勤政的一位皇帝。为什么这么说呢？首先雍正皇帝在位不到13年（12年零8个月），为了施展自己的治国方略，雍正从一开始上位以后就励精图治，每天工作到很晚，据身边的太监说，雍正皇帝每天的睡眠时间是四个小时。更为疯狂的是，一年365天，雍正皇帝，只休息1天。</p>
                <p style="text-indent: 2em;">他甚至规定，密折制度从近臣扩展到全体臣工。本来每天批的折子都够多了，这一下子，折子数量又以几何数字暴涨，想想都酸爽！当时，很多人认为，皇帝一人承担一朝之事简直开玩笑，肯定坚持不了几天。而雍正皇帝用实际行动回答了所有质疑。</p>
                <p style="text-indent: 2em;">据历史史料记载，雍正皇帝从他继位到他驾崩的时候，一共批了192000件奏章。每年就是14700件。从博物院中，我们可以看到雍正皇帝的批阅奏章，有的批阅就达到了几百个字，甚至上签字。据历史统计，雍正皇帝在，13年的时间里，一共留下了1000万字的批文。</p>
                <p style="text-indent: 2em;">
                    <strong>
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇帝都很勤奋 结果治理能力太差灭亡</strong></p>
                <p style="text-indent: 2em;">为什么虽然有的
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>的皇帝很勤奋，但还是亡了国？我们也看了很多回答，感觉都没有说到点子上。皇帝勤奋是没有错误的，但是，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>最后的皇族其实是因为不够优秀，不够勤奋才失败的。</p>
                <p style="text-indent: 2em;">为了让皇上能专心处理政务，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>还不会让皇上拥有兴趣爱好，在以前的时候很多帝王都有一些兴趣爱好，而很多皇上都太沉迷于自己爱好了，这样也就会减少处理事务的时间，从而导致国家变得衰弱。像是宋徽宗，就热爱书法、绘画，从来不关系大事，最后
                    <a href="http://m.114.xixik.com/chaodai/song/" style="color: rgb(0, 0, 0);" title="宋朝">宋朝</a>被金国轻易击败；明熹宗也是这样，他非常喜欢做木工并且他将全部的精力都用来做木工，如果他是平常人的话，他将是一个出色的木匠，但是他是皇上，他从来不关心政务，让权力被太监掌握，很多大臣都被杀了，朝廷也乱糟糟的，最后
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="color: rgb(0, 0, 0);" title="明朝">明朝</a>也被灭了。</p>
                <p style="text-indent: 2em;">为了避免这一点，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>从最源头的地方抓起，那就是让每一个皇子都用心学习，让他们没有兴趣爱好。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>的皇子很辛苦，每天4点就要起来温习，接着去上课，一直要学到6点才有自己的时间，而且他们机会没有休息的日子，除了生日、过年以外，他们每天都要上课，并且老师对他们也非常的严厉。有一次有一位皇子犯错了，老师就让他跪下，这个时候乾隆正好来了，他就跟这位老师说：“他要是学好了，那就能成为帝王，而他就算学不好也是皇子，你让他跪下是不是不符合君臣的规范。”</p>
                <p style="text-indent: 2em;">而老师却说：“如果他学习我的知识，那将来就是一位明君像尧舜一样，而他如果不学的话，就算是皇上那也是昏君，那就跟桀纣没区别了。”皇上听了以后觉得很对，就让皇子继续跪着。还有一点，就是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>的会挑选最出色的皇子做皇上，也就是学的最好的。在之前的
                    <a href="http://m.114.xixik.com/chaodai/" style="color: rgb(0, 0, 0);" title="历史朝代顺序表">朝代</a>都是让大儿子做皇上，就算是傻子也能做，比如晋惠帝。而
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>却不一样，他们从来都是选择最优秀的一个，也就是学习最好的那位皇子做皇上。</p>
                <p style="text-indent: 2em;">所以，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>最大的特点就是几乎所有的皇子都接受过完整的皇族教育。而且，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇族教育严格到了什么程度呢？道光的大阿哥就因为威胁要杀死老师，而被道光处决。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>到了后期衰落的一个重要原因就是，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇族后来子嗣太少，到了同治，光绪时代，皇帝都没有一起在上书房接受了完整皇族教育的兄弟，到了宣统更只是一个小孩。要知道，从小在皇宫上书房接受了完整皇族教育的亲王们在
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>是稳定统治，非常关键的人群。</p>
                <p style="text-indent: 2em;">到了宣统的时候，那些王爷没有一个接受过真正完整的皇族教育，都是旁系出身，水平自然是严重下降。如果，同治皇帝，光绪皇帝有几个亲兄弟，一起接受了上书房教育，
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>到1910年也不会大权旁落。</p>
                <p style="text-indent: 2em;">
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="color: rgb(0, 0, 0);" title="明朝">明朝</a>几乎所有皇帝都不合格的关键，就是
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="color: rgb(0, 0, 0);" title="明朝">明朝</a>很多皇帝都没有接受过完整的皇族教育。天启皇帝，崇祯皇帝，嘉靖皇帝，正德皇帝，弘治皇帝等等，都没有接受过正规皇族教育。包括万历皇帝也一样，没有接受完整皇帝教育。竟然为了节约20万两白银，一句话就把
                    <a href="http://m.114.xixik.com/chaodai/ming/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="明朝">明朝</a>官营军马场全部关门了，这直接导致了明军战斗力全面加速下降。</p>
                <p style="text-indent: 2em;">
                    <strong>虽然
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>皇帝算勤勉，为什么最后
                        <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>还是被灭了？</strong>可是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>做了这么多依然打不过外敌？因为在国家内忧外患之际勤政用处不大，并且纵观中后期皇帝虽不能说昏君，但肯定是庸君，他们没有才能也没有胆魄来挑起这个国家存亡之秋的重担，试问焉能管理好这片大好河山？</p>
                <p style="text-indent: 2em;">这是因为到了
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="color: rgb(0, 0, 0);" title="清朝">清朝</a>的时候局势已经不一样了。清朝后期面临的世界格局是其他朝代不能比拟的。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>的皇上虽然也是很辛勤的处理事务，但是他们依然是延续之前的方法，而他们并没有意识到，世界已经发生变化了，这些皇上认为
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>物资丰富，管好自己一亩三分地的，不需要跟外面的实力交流依然能生活得很好，可是这一次世界的变革却不一样了，当康熙还学着古代南征北战吭哧吭哧为清朝打下铁桶江山的时候，西方正在进行工业革命，欧洲人借助汽轮将这个世界变了个样，并且完全碾压了
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>。
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>不想跟外面做生意，而外面却想要
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>的市场，因此双发爆发了战争，结果就是
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>惨败，当
                    <a href="http://m.114.xixik.com/chaodai/qing/" style="text-indent: 2em; color: rgb(0, 0, 0);" title="清朝">清朝</a>想要变革的时候已经太晚了。在这个时候，光勤劳已经不够了，还必须要有格局要看清局势，不然只能被别人超越碾压。</p>
                <p style="text-indent: 2em;">当西方国家打进中国土地时，清政府最首要的防备目标却不是他们，而是控制下的汉族子民，在满清的心中，汉人永远都是最大的敌人。满清政府的目光短浅，一向活在天朝上国的梦境当中，却不知道与时俱进，其时八国联军打进北京紫禁城，展现在他们眼里的是肯定不下于西方的先进武器和物器，可是却仅仅供君主赏玩算了。最后，
                    <a href="http://m.114.xixik.com/minority/" style="color: rgb(0, 0, 0);" title="民族">民族</a>对立日益加剧，土地吞并不断，国人奴性思维严峻，更有后期慈禧当政期间的骄奢淫逸，自己吃苦犹在国家建设之上，清廷贪污腐败，吏治紊乱。</p></div>
