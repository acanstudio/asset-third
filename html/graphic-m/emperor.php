                    <a href="http://www.xixik.com/content/4a5f47269ed5cea9" style="text-indent: 2em;" title="清朝皇帝的谥号">清朝皇帝在谥号上不要脸到了令人发指的地步</a>。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>1、清太祖——
                        <strong style="text-indent: 2em;">努尔哈赤（称汗）</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/2a1c3-6517.jpg" /></p>
                <p style="TEXT-INDENT:2em">明万历十一年（1583年），努尔哈赤（1559—1626年）不屈奋起，25岁时以父、祖遗甲十三副起兵，“自中称王”。明万历四十四年（公元1616年），在赫图阿拉称“覆育列国英明汗”，国号大金（史称后金），年号天命。努尔哈赤兵势渐强，势力日增，万历四十六年（1618）以“七大恨”祭天，誓师征明。天命十一年（1626年）与明将袁崇焕在宁远交战中，大败而回并受伤，于八月死去。终年68岁，葬于沈阳城东，称之“福陵”。清朝建立后，尊为清太祖，谥曰承天广运圣德神功肇纪立极仁孝睿武端毅钦安弘文定业高皇帝。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>2、清太宗——
                        <strong style="text-indent: 2em;">皇太极</strong>（称汗后称帝）</strong></p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/36ec1-1595.jpg" /></p>
                <p style="TEXT-INDENT:2em">天聪 爱新觉罗·皇太极（1592—1643年），努尔哈赤第八子，满族。天命十一年（1626年）九月，继后金汗位，以次年为天聪元年。天聪十年（1636年）四月，皇太极在沈阳称帝，建国号大清，改年号为崇德，并以是年为崇德元年。前后在位17年。在位期间，注意发展生产，增强兵力，不断对明作战，确定满族族名，建立清国，为清王朝的确立和后来统一中国打下了坚实基础。庙号太宗，谥号应天兴国弘德彰武宽温仁圣睿孝敬敏昭定隆道显功文皇帝，葬于沈阳昭陵。死后其第九子爱新觉罗·福临即位。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>3、清世祖——
                        <strong style="text-indent: 2em;">顺治皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/8913e-5388.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·福临（1638年--1661年），皇太极第九子，清入关后第一位皇帝。6岁继帝位，由叔父睿亲王多尔衮摄政，14岁亲政。清初满汉民族矛盾与阶级矛盾极为激烈，而至顺治朝结束时，清廷击败了各种抗清势力，完成了全国的统一。</p>
                <p style="TEXT-INDENT:2em">顺治帝天资聪颖，读书勤奋，他吸收先进的汉文化，审时度势，对成法祖制有所更张，且不顾满洲亲贵大臣的反对，倚重汉官。为了使新兴的统治基业长治久安，他以明之兴亡为借鉴，警惕宦官朋党为祸，重视整饬吏治，注意与民休息，取之有节。但他少年气盛，刚愎自用，急噪易怒，当他宠爱的董妃去世后，转而消极厌世。顺治十八年（1661年）顺治帝驾崩，终年24岁。庙号世祖，谥号体天隆运定统建极英睿钦文显武大德弘功至仁纯孝章皇帝，葬于清东陵之孝陵，遗诏传位第三子玄烨。他是清朝历史上唯一公开归依禅门的皇帝。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>4、清圣祖——
                        <strong style="text-indent: 2em;">康熙皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/27a99-7300.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·玄烨（1654--1722年），顺治皇帝第三子，清入关后第二位皇帝。自幼勤奋好学，文韬武略样样精通，清除螯拜，撤除三藩，收复台湾，驱逐了沙俄势力，又平息蒙藏地区动乱，平定准葛尔叛乱等一系列军事行动中或御驾亲征，或决胜千里，充分显示了他的军事才能，加强了多民族国家的稳定和统一。慎选人才，表彰清官，修治河道，笼络汉族知识分子等行为，又反映了康熙是一个出色的政治家和睿智的君主。</p>
                <p style="TEXT-INDENT:2em">在经济和文化建设上，康熙也创下对后世产生积极影响的重大业绩，
                    <a href="http://www.xixik.com/content/ecfa518db7c1e617" title="什么康乾盛世不存在的！">开创了中国封建社会最后一个盛世——康乾盛世</a>。这只是相对于弱清的“盛世”，虽然号称强盛，
                    <a href="http://www.xixik.com/content/3fc7eb4df8e7b4cf" title="看似平等的条约《尼布楚条约》却是失地条约">但是割地却从他开始——看似平等的条约《尼布楚条约》</a>却是把300多万平方公里土地割让给俄罗斯的失地条约。
                    <a href="http://www.xixik.com/content/b029134123f1f1a3" target="_blank" title="不和亲不纳贡不称臣不割地 大明朝才最有骨气">不和亲不纳贡不称臣不割地 大明朝才最有骨气</a>。和康熙的政治生活相比，他的家庭生活并不美满，诸皇子夺储之争，使他心力憔悴。康熙尽管是以为励精图治，关心民生的好皇帝。当然，作为站在历史潮流前面，引导国家，民族快速进步的伟大人物，他则逊色得多了。所以什么“千古一帝”之类的都过了。</p>
                <p style="TEXT-INDENT:2em">
                    <a href="http://www.xixik.com/content/0e77741d191c0d2b" title="康熙皇帝是中国历史上在位时间最长的皇帝">
                        <strong>康熙皇帝是中国历史上在位时间最长的皇帝</strong></a>，在位61年。庙号圣祖。康熙帝是统一的多民族国家的捍卫者，奠定了清朝兴盛的根基。康熙六十一年（1722年）农历十一月十三日崩于畅春园，终年68岁。庙号圣祖，谥号合天弘运文武睿哲恭俭宽裕孝敬诚信功德大成仁皇帝，葬于清东陵之景陵，传位于第四子胤禛。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>5、清世宗 ——
                        <strong style="text-indent: 2em;">雍正皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/c45b4-6074.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·胤禛（1678—1735年），康熙皇帝第四子，清入关后的第三位皇帝。康熙61年，45岁的胤禛继承帝位，在位13年。他对有碍于皇权的反对势力大加挞伐，有效地改善了吏治，增加了国库收入，为乾隆朝社会的繁荣奠定了雄厚的基础。</p>
                <p style="TEXT-INDENT:2em">雍正是在康乾盛世前期——康熙末年社会出现停滞的形式下登上历史舞台的。复杂的社会矛盾，为雍正提供了施展抱负和才干的机会。他有步骤地进行了多项重大改革，高瞻远瞩，又惟日孜孜，励精图治，十三年中取得了卓有成效的业绩，为后代的乾隆打下了扎实雄厚的基础，使“康乾盛世”在乾隆时期达到了顶峰。</p>
                <p style="TEXT-INDENT:2em">他的历史地位，同乃父康熙和乃子乾隆相比，毫不逊色。尽管他猜忌多疑，刻薄寡恩，统治严酷，但比起他的业绩来，毕竟是次要的。雍正十三年（1735年）崩于圆明园，终年58岁，庙号世宗，谥号敬天昌运建中表正文武英明宽仁信毅睿圣大孝至诚宪皇帝，葬清西陵之泰陵，传位于第四子弘历。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>6、清高宗——
                        <strong style="text-indent: 2em;">乾隆皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/1b544-5261.jpg" /></p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/46f0d-7931.jpeg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·弘历（1711—1799年），雍正皇帝第四子，清朝入关后第四位皇帝。他在将清朝的康乾盛世推向顶峰的同时，也亲手将它带向低谷，他是影响中国18世纪以后历史进程的重要皇帝。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/276b4-1146.jpg" style="margin: 10px; float: right; width: 400px;" /></p>
                <p style="TEXT-INDENT:2em">乾隆即位之初，实行宽猛互济的政策，务实足国，重视农桑，停止捐纳，平定叛乱等一系列活动中，充分体现了他的文治武功，乾隆帝向慕风雅，精于骑射，笔墨留于大江南北，并是一个有名的文物收藏家。清宫书画大多是他收藏的，他在位期间主持编纂的《四库全书》共收书3503种，79337卷，36304册，其卷数是《永乐大典》的三倍。同时销毁对清朝不利的书记，明代档案，篡改书籍，
                    <a href="http://www.xixik.com/content/bc37004e43fe0e1b" title="《四库全书》说是修书，实则毁书">《四库全书》说是修书，实则毁书</a>，是中国文化一大浩劫。</p>
                <p style="TEXT-INDENT:2em">但乾隆为人重奢靡，六下江南，晚年时国库财用耗竭，并重用贪官和绅，以至农民起义在其晚年也已层出不穷，是清王朝从强盛走向衰败的标志。</p>
                <p style="TEXT-INDENT:2em">在位60年，乾隆六十年（1795年），乾隆帝禅位于第十五子颙琰。退位后又当了三年太上皇，嘉庆四年（1799年）2月7日卒于紫禁城养心殿，终年89岁。庙号高宗，谥号法天隆运至诚先觉体元立极敷文奋武钦明孝慈神圣纯皇帝。葬于清东陵之裕陵。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>7、清仁宗——
                        <strong style="text-indent: 2em;">嘉庆皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/04acf-1454.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·顒琰（1760—1820年），乾隆皇帝第十五子，清朝入关后第五位皇帝。乾隆六十年（1795年）九月公开立为皇太子，次年元旦，受弘历内禅即位，以是年为嘉庆元年，时年27岁。嘉庆四年（1799年）正月，弘历死，始亲政。顒琰亲政后，立即铲除贪赃枉法蠹国肥私的权臣和珅，使人心大快，复多方采取措施，力图保持康乾之盛世，但内忧外患重重，积重难返。由于腐败势力甚强，又缺乏新生机制，终难摆脱江河日下的命运。</p>
                <p style="TEXT-INDENT:2em">嘉庆帝是一位勤政图治的守成君主。他亲政后采取的一系列政策，措施，对于改变乾隆后期的种种弊政起了一定的作用，但没有，也不可能从根本上扭转清代中衰之势。从嘉庆帝个人来说，他始终开不出一个根治日趋严重的腐化和怠惰的药方，对一大批"尸禄保位"的官僚只能警告，恫吓，最终徒呼奈何而已。他对西方殖民主义者的侵略有一定的认识，但对于一个日趋衰弱的封建的古老国家，不可能真正有效地对付外来侵略者，此后只能沿着衰败的道路滑下去。</p>
                <p style="TEXT-INDENT:2em">他在位期间正值世界工业革命兴起的时期，也是清朝由盛转衰的时期。这时期发生了白莲教之乱，八旗生计、河道漕运等问题也日益凸显，鸦片亦流入中国。清朝出现了中衰。 嘉庆二十五年（1820年）驾崩，在位25年，庙号仁宗，谥号受天兴运敷化绥猷崇文经武光裕孝恭勤俭端敏英哲睿皇帝，葬于清西陵之昌陵，传位于第二子旻宁。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>8、清宣宗——
                        <strong style="text-indent: 2em;">道光皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/19de7-9618.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·旻宁（1782年9月16日—1850年2月25日），原名绵宁，即位后改为旻宁。嘉庆皇帝第二子，是清军入关后的第六位皇帝。是清朝唯一以嫡长子身份继承皇位的皇帝。嘉庆二十五年（1820年）七月即位，以次年为道光元年，时年29岁。在位30年。在位期间，由于国力开始衰落，故极力提倡节俭，改革盐政，部分弛禁开矿，并整顿吏治。但由于腐败成风，阻力过大，奏效甚微。对鸦片之害，他最初力主抵抗，但因他本人对时势无知，主要大臣懦弱无能，战略动摇无定，反而迫害禁烟主力，不能抵抗列强的侵略，酿成百年遗憾。&#160;</p>
                <p style="TEXT-INDENT:2em">才智平庸的道光帝徒以俭德著称。他处于历史转折的关键时刻，“守其常而不知其变”。来自东南海上的鸦片流毒和英军入侵，使他寝食不安。他想严厉禁烟，也曾下决心抗击侵略者，但他不知英国来自何方，不知殖民主义为何物。平素无知人之明，临危无应变之策，以至战守茫然，毫无方略，只能在自恨自愧中顿足叹息，结果忍辱接受英国的城下之盟，签定了近代史上第一个不平等条约《中英南京条约》。此后十年他苟安姑息，得过且过，没有任何学习西方，振兴王朝的措施。</p>
                <p style="TEXT-INDENT:2em">道光帝朝纲独断，事必躬亲，但内政事物，如吏治，河工，漕运，禁烟等均无起色。勤政图治而鲜有作为，正是他一生的悲剧所在。道光三十年（1850年）正月十四日驾崩。在位30年，终年69岁。庙号宣宗，谥号效天符运立中体正至文圣武智勇仁慈俭勤孝敏宽定成皇帝，葬于清西陵之慕陵，传位第四子奕詝。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>9、清文宗——
                        <strong style="text-indent: 2em;">咸丰皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/803c0-1829.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·奕詝（1831年7月17日—1861年8月22日），清朝入关后第七位皇帝，道光皇帝第四子。道光三十年（1850年）正月即位，以次年为咸丰元年，时年20岁。在位11年。在位期间，正逢清朝乱世，国库空虚，危机四伏。即位不到一年，太平天国即在金田起义，且三年左右就危及北京，咸丰帝屡派兵将镇压太平天国及各地农民起义。战火未息，英法联合侵略军又迅速攻占天津、北京，奕詝从圆明园逃往避暑山庄。只命奕欣留北京与英法讲和。侵略军竟野蛮地焚掠了圆明等园。不得已奕詝与英法等国签订了数宗不平等条约。奕詝纵有重整江山之心，已无回天之力。</p>
                <p style="TEXT-INDENT:2em">咸丰帝即位后便勤于政事，大手笔的对朝政改革。为了挽救统治危机，咸丰帝颇思除弊求治。他任贤去邪，企图重振纲纪。重用汉族官僚曾国藩，依靠其训练指挥的汉族地主武装镇压太平天国和捻军起义。提拔敢于任事的肃顺，支持肃顺等革除弊政。同时，罢斥了道光朝军机大臣穆彰阿、耆英等。但此时的大清帝国内忧外患不断，最后以签定一系列不平等条约收场。咸丰朝以后也因企图重新扭转对内交外困的局面而开启洋务运动。</p>
                <p style="TEXT-INDENT:2em">咸丰帝在位十一年，咸丰十一年（1861年）8月22日崩于承德避暑山庄，终年31岁。庙号文宗，谥号协天翊运执中垂谟懋德振武圣孝渊恭端仁宽敏庄俭显皇帝，葬于定陵。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>10、清穆宗——
                        <strong style="text-indent: 2em;">同治皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/2efeb-1981.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·载淳（1856年4月27日－1875年1月12日），清朝第十位皇帝，也是清军入关以来第八位皇帝，咸丰六年（1856年）三月二十三日（公历4月27日）生于北京紫禁城储秀宫，为清文宗咸丰帝唯一的儿子，生母为孝钦显皇后叶赫那拉氏（慈禧）。5岁即帝位，按咸丰帝诏命，由肃顺等八大臣辅政。慈禧联合恭亲王奕訢发动政变，逮治了肃顺等八大臣。政变之后，慈安与慈禧（咸丰帝皇后）垂帘听政。并取消“祺祥”年号，改用“同治”，隐含两宫太后与众大臣共理朝政之意。终身成为其生母慈禧皇太后垂帘听政的傀儡。</p>
                <p style="TEXT-INDENT:2em">同治帝在位14年，在此期间，清朝政府依靠曾国藩、李鸿章、左宗棠等一批重臣镇压了太平天国起义等一系列的农民起义。也办了一些所谓的"洋务新政"。但这些与同治皇帝都没多大关系。当时的统治者实际上是慈禧。同治帝幼年是一个少不更事的顽童，亲政以后，作为一个青年皇帝，确实是辜负了朝野上下对他的殷切期望。亲政两年后，死于天花，另一说死于梅毒。同治帝无后，慈禧即挑出咸丰之弟醇亲王爱新觉罗奕譞之子载湉为帝，是为光绪。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>11、清德宗——
                        <strong style="text-indent: 2em;">光绪皇帝</strong></strong>
                </p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/b5c0f-6836.jpg" /></p>
                <p style="TEXT-INDENT:2em">&#160;爱新觉罗·载湉（1871年8月14日—1908年11月14日），醇亲王奕譞之子，清军入关后第九位皇帝，在位年号光绪，史称光绪帝。父亲醇亲王奕譞是道光帝的第七子，生母叶赫那拉·婉贞为慈禧皇太后亲妹，这种特殊的家庭环境，使他在同治病故之后被指定为皇帝。因其冲龄践祚，导致慈禧太后第二次垂帘听政。在其亲政期间，甲午之战中他力主反击，大力支持维新变法，但因变法失败而遭幽禁，直至去世。在位34年。</p>
                <p style="TEXT-INDENT:2em">光绪帝19岁亲政，他富有年轻人的进取精神，愿意接受新思想，不甘作亡国之君，积极支持变法，一度成为维新派心中的"救世主"。但变法危及封建守旧势力的利益，遭到以慈禧为主的清室贵族的阻挠。戊戌变法的失败，使清王朝改变旧章的一线生机被扼杀。</p>
                <p style="TEXT-INDENT:2em">光绪帝没有勇气冲破封建伦理思想的束缚，“天颜戚戚，常若不悦”，心境悲怆，终其一生是屈辱和哀怨的悲剧命运。光，光大；绪，未竟之功业。“光绪”即“光大未竟之功业”。“光绪”这个年号反映了清廷重振国力的愿望。八国联军占领北京时，慈禧只好挟光绪帝仓皇逃到西安。义和团运动后，各地反清武装起义此起彼伏，民主革命思潮在全国广泛传播，清王 朝濒于覆灭的边缘。</p>
                <p style="TEXT-INDENT:2em">他在位34年，光绪十三年病死，终年38岁，庙号德宗，葬于河北易县崇陵。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>12、宣统皇帝</strong></p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/ec5e4-7284.jpg" /></p>
                <p style="TEXT-INDENT:2em">爱新觉罗·溥仪（1906年2月7日—1967年10月17日），字耀之，号浩然。清朝末代皇帝，也是中国历史上最后一个皇帝。也称清废帝或宣统帝。醇亲王奕譞之孙，载沣长子，母亲苏完瓜尔佳氏。光绪三十四年（1908年）十月，由于光绪帝无后，议定立三岁的溥仪为帝，半个多月后，溥仪在太和殿即位，明年改元“宣统”。宣，大。“宣统”即“大统”。宣统”二字有为清王朝统治的正当性作辩护的意思。1909年到1912年、1917年7月1日到1917年7月12日两次在位。
                    <a href="http://www.xixik.com/content/a5963ab00dfe8f3b" target="_blank" title="大清就是要亡了 跟溥仪登基时生父说的话没关系">大清就是要亡了 跟溥仪登基时生父说的话没关系</a></p>
                <p style="TEXT-INDENT:2em">1911年辛亥革命爆发，1912年2月12日被迫退位，清朝统治结束。此后，以逊帝身份居住在皇宫。1924年11月5日，被冯玉祥驱逐出宫。九·一八事变之后在日本人控制下做了伪满洲国的傀儡皇帝，年号康德（1934年—1945年），所以又称“康德皇帝”。1945年8月15日日本投降。8月17日，溥仪在沈阳准备逃亡时被苏联红军俘虏，被带到苏联。1950年8月初被押解回国，在抚顺战犯管理所学习、改造。1959年12月4日受到特赦令并成为全国政协委员。</p></div>
