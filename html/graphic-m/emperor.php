                <p style="TEXT-INDENT:2em">
                    <a href="http://m.114.xixik.com/chinese-emperors/qing/" style="color:#000" title="清朝皇帝列表">清朝皇帝</a>和历史上王朝一样实行世袭。太祖努尔哈赤之后的11帝都是他的子孙。清朝是少数民族政权，入关以前并不遵循汉族的传统礼法，袭位方式与历代不大同。</p>
                <p style="TEXT-INDENT:2em">
                    <strong>
                        <a href="http://www.xixik.com/content/07ee5069a12fc62b" title="嫡长子继承制度 最后能够即位的嫡长子少之又少">历代由皇帝的嫡长子继位，并且要先预立为太子</a></strong>。清朝不守这个规矩，继位的不论是否皇长子，除了载淳外，无一是长子。载淳曾有一弟，出生后未命名就死了，因此无人与他争王位。继位也不一定要先预立为太子。清朝的继位法几经变化。开始实行汗位推选继承制，太宗、顺治皇帝和康熙皇帝都是由皇室贵族推选的。康熙皇帝想效法汉家礼制，开始立嫡长子胤礽为皇太子，结果因为他在位时间过长，导致太子没有耐性等下去而生了异心，又把他给废了。因此，这个胤礽因此成为了中国历史上最后一位皇太子，也是在位时间最长的皇太子。后来是皇四子胤禛争到了皇位。另外太子因为是法定的接班人，所以日益骄横、目中无人，在朝中大失人心，与康熙皇帝本人也产生的矛盾。于是其他的皇子借着皇权与储权冲突的机会，纷纷觊觎皇位，互相明争暗斗，形成了
                    <a href="http://www.xixik.com/content/61a1cfa07e60838f" title="九龙夺嫡">康熙晚年的“九龙夺嫡”局面</a>，最后太子两度被废，没能继承皇位。</p>
                <p style="TEXT-INDENT:2em">民间或者野史里说，月黑风高夜，四阿哥将康熙遗诏中“传位十四子”中的“十”字改成了“于”字，就变成了“传位于四子”。纯属扯淡，完全不着边际，驳斥的论据，遗诏要分满文、汉文、蒙文三份，能改汉文，满蒙文根本改不了。又如，遗诏的格式并不是“传位某子”，而是传位于皇某子胤某。而且，“于”字在古代要写成“於”。</p>
                <p style="text-align: center;">
                    <img src="<?= Yii::getAlias('@tasseturl'); ?>/graphic/m/img/a59d5-2710.jpg" style="margin: 10px; float: right; width: 400px;" /></p>
                <p style="TEXT-INDENT:2em">鉴于过去几代皇子争位刀光剑影的残酷教训，雍正皇帝发明了一种秘密建储（储，储君，太子）法：把挑选出来准备继位的皇子姓名写好，放于匣中，置诸乾清宫正中顺治帝御书“正大光明”匾之后。皇帝死后，取出宣读，新君即位。这样一来，就让皇子们都觉得自己有希望，从而不会相互争斗，也不会为了皇位而逼宫谋害父皇，同时避免了接班人成为众矢之的，而皇帝也可以放心地培养皇家子弟，予以重任，不会出现明朝皇子昏庸无能的情况，可谓一举多得。这一制度的优越性使其成为定制，后世的清朝皇帝基本都遵循此例。这种办法用到道光。这段时间秘密建储也有中途改变的。乾隆帝即位后立皇次子永琏为太子，乾隆三年永琏夭亡，乾隆帝改立皇十五子永琰（永琰后改为颙琰）。咸丰传位是临终召王大臣承写珠谕，立皇长子为皇太子。至于后来同治帝死后由载湉继位，是为光绪帝，光绪帝死后由溥仪继位，是为宣统帝，则是由慈禧太后懿旨确定的醇亲王载沣。</p>
